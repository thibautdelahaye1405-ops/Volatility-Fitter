# Figure 3 (wrong-way): interpolating the dense curve in the wrong clock.
# ---------------------------------------------------------------------------


def figure_interp() -> None:
    # Quoted expiries straddling the event; flat 20% vol on the event clock.
    t_nodes = np.array([0.10, 0.20, 0.35, 0.60, 1.00])
    tau_nodes = np.array([weighted_variance_years(float(t), EVENTS)
                          for t in t_nodes])
    w_nodes = SIGMA**2 * tau_nodes

    tq = np.linspace(0.05, 1.05, 500)
    tau_q = np.array([weighted_variance_years(float(t), EVENTS) for t in tq])
    w_right = interp_total_variance(tau_q, tau_nodes, w_nodes)
    w_wrong = interp_total_variance(tq, t_nodes, w_nodes)
    iv_right = 100 * np.sqrt(w_right / tq)
    iv_wrong = 100 * np.sqrt(w_wrong / tq)
    macro("emcinterpgapbp", "%.0f", 1e2 * np.max(np.abs(iv_wrong - iv_right)))

    fig, ax = plt.subplots(figsize=(6.9, 3.9))
    iv_nodes = 100 * np.sqrt(w_nodes / t_nodes)
    ax.plot(tq, iv_right, color=TEAL, lw=2.0,
            label="interpolated in the variance clock (production)")
    ax.plot(tq, iv_wrong, color=RUST, lw=1.8, ls="--",
            label="interpolated in calendar time")
    ax.plot(t_nodes, iv_nodes, "o", ms=5, color="black", zorder=5,
            label="quoted expiries")
    ax.axvline(TE, color=PALETTE["ink"], lw=0.8, alpha=0.5)
    ax.annotate("event", xy=(TE + 0.01, float(iv_right.min()) - 0.12),
                fontsize=8.5, color=SLATE)
    ax.set_xlabel(r"calendar time $T$ (years)")
    ax.set_ylabel("ATM implied volatility (%)")
    ax.legend(fontsize=8.5, loc="upper right")
    save(fig, OUT / "fig_emc_interp.pdf")


# ---------------------------------------------------------------------------
# Figure 4: identifiability audit — planted vs recovered event sizes.
# ---------------------------------------------------------------------------


def figure_ident() -> None:
    t_nodes = np.array([0.10, 0.20, 0.35, 0.60])
    planted = np.array([1.0, 2.0, 3.0, 5.0, 8.0])

    def sweep(sigma: float) -> np.ndarray:
        rec = []
        for n_days in planted:
            tau_nodes = np.array([weighted_variance_years(
                float(t), [(0.15, float(n_days))]) for t in t_nodes])
            ev = autocalibrate_events(t_nodes, sigma**2 * tau_nodes,
                                      n_events=3)
            rec.append(sum(n for te, n in ev if 0.10 < te <= 0.20))
        return np.array(rec)

    rec_hi = sweep(0.40)
    rec_lo = sweep(0.20)
    macro("emcidentshrink", "%.2f", float(np.max(planted - rec_hi)))
    macro("emcidentlowfloor", "%.0f",
          float(planted[rec_lo <= 0.0].max()) if np.any(rec_lo <= 0.0) else 0)

    ev_flat = autocalibrate_events(t_nodes, 0.40**2 * t_nodes, n_events=3)
    macro("emcidentflat", "%d", len(ev_flat))

    fig, ax = plt.subplots(figsize=(6.6, 3.9))
    lim = (-0.3, planted.max() * 1.1)
    ax.plot(lim, lim, color=PALETTE["ink"], lw=0.8, ls=":")
    ax.plot(planted, rec_hi, "o", ms=7, color=TEAL, mfc="none", mew=1.8,
            label=r"$\sigma=40\%$: recovered (mild shrinkage)")
    ax.plot(planted, rec_lo, "x", ms=7, color=RUST, mew=1.8,
            label=r"$\sigma=20\%$: below materiality until large")
    ax.plot([0.0], [len(ev_flat)], "s", ms=7, color=SLATE,
            label="flat term structure: no events")
    ax.set_xlabel("planted event size (extra days)")
    ax.set_ylabel("recovered size (extra days)")
    ax.set_xlim(*lim)
    ax.set_ylim(*lim)
    ax.legend(fontsize=8.5, loc="upper left")
    save(fig, OUT / "fig_emc_ident.pdf")


