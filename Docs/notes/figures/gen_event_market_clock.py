# Appendix verification: the clock listing against production.
# ---------------------------------------------------------------------------


def listing_weighted_variance_years(t_cal, events, normalize=False, dpy=365.0):
    if t_cal <= 0:
        return t_cal
    tau_days = t_cal * dpy + sum(n for te, n in events
                                 if te <= t_cal and n > 0)
    if normalize:
        e1 = sum(n for te, n in events if te <= 1.0 and n > 0)
        tau_days *= dpy / (dpy + e1)
    return tau_days / dpy


def verify_listings() -> None:
    rng_t = np.linspace(0.01, 2.0, 97)
    evs = [(0.1, 2.0), (0.25, 4.0), (0.9, 1.5), (1.4, 3.0)]
    d = 0.0
    for norm in (False, True):
        for t in rng_t:
            d = max(d, abs(listing_weighted_variance_years(float(t), evs, norm)
                           - weighted_variance_years(float(t), evs,
                                                     normalize=norm)))
    sci_macro("emcrefagree", d)
    macro("emcdays", "%.0f", DAYS_PER_YEAR)


