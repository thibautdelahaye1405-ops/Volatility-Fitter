"""Figure suite for the market's-own-clock edition of Note 11 (event time).

Run from the repository root with the project virtual environment::

    .venv\\Scripts\\python.exe Docs\\notes\\figures\\gen_event_market_clock.py

Every clock value, interpolation and solved calendar is produced by
PRODUCTION machinery (``volfit.calib.weighted_time`` and
``volfit.calib.event_autocalib``); local formulae define only synthetic
targets.  The hero figure runs the production auto-calibrator on the real
AAPL ATM term structure of the standing export
``tmp/volfit_surfaces_20260718_1324.json``.

Outputs, written next to this script:

* ``fig_emc_hero.pdf``   -- reading the clock off live AAPL: forward
                            variance per calendar vs per event-time unit,
                            with the solver's installed extra days;
* ``fig_emc_clock.pdf``  -- the dilated clock and the term-structure
                            read-off (parity with the original edition);
* ``fig_emc_interp.pdf`` -- wrong-way demo: interpolating the dense curve
                            in calendar time vs in the variance clock;
* ``fig_emc_ident.pdf``  -- identifiability audit: planted vs recovered
                            event sizes, and the flat-input null;
* ``event_market_tables.tex`` / ``event_market_numbers.json``.
"""

