MACROS: list[str] = ["% Auto-generated by gen_event_market_clock.py — do not edit."]
NUMBERS: dict = {}


def macro(name: str, fmt: str, value) -> None:
    MACROS.append((r"\newcommand{\%s}{" + fmt + "}") % (name, value))
    NUMBERS[name] = float(value) if isinstance(value, (int, float, np.floating)) else value


def sci_macro(name: str, value: float) -> None:
    v = max(abs(value), 1e-17)
    e = int(np.floor(np.log10(v)))
    m = v / 10.0**e
    MACROS.append(r"\newcommand{\%s}{\ensuremath{%.1f\times10^{%d}}}"
                  % (name, m, e))
    NUMBERS[name] = float(value)


def fwd_var(t: np.ndarray, w: np.ndarray, tau=None) -> np.ndarray:
    """Forward variance per unit of the given clock (prepend the origin)."""
    x = t if tau is None else tau
    xx = np.concatenate([[0.0], x])
    ww = np.concatenate([[0.0], w])
    return np.diff(ww) / np.diff(xx)


# ---------------------------------------------------------------------------
# Figure 1 (hero): reading the clock off the live AAPL term structure.
# ---------------------------------------------------------------------------


def figure_hero() -> None:
    fixture = REPO / "tmp" / "volfit_surfaces_20260718_1324.json"
    if not fixture.exists():
        raise FileNotFoundError(
            f"standing reference export not found: {fixture} — regenerate via "
            "the app's surfaces export (see memory: reference-live-fixture)")
    doc = json.loads(fixture.read_text(encoding="utf-8"))
    aapl = next(tk for tk in doc["tickers"] if tk["ticker"] == "AAPL")
    ts, w0s, labels = [], [], []
    for n in aapl["nodes"]:
        ks = np.array([c["k"] for c in n["curve"]])
        ws = np.array([c["w"] for c in n["curve"]])
        ts.append(n["t"])
        w0s.append(float(np.interp(0.0, ks, ws)))
        labels.append(n["expiry"][2:])  # YY-MM-DD
    ts, w0s = np.array(ts), np.array(w0s)

    # Production auto-calibration: events allowed before the first three
    # expiries; the last interval anchors the tail (the shipped design).
    events = autocalibrate_events(ts, w0s, n_events=3)
    taus = np.array([weighted_variance_years(float(t), events) for t in ts])
    fv_cal = fwd_var(ts, w0s)
    fv_evt = fwd_var(ts, w0s, tau=taus)
    macro("emcheronodes", "%d", ts.size)
    macro("emcherodays", "%.1f", sum(n for _, n in events))
    macro("emcheroeventcount", "%d", len(events))
    macro("emcherospreadbefore", "%.0f",
          1e4 * (fv_cal.max() - fv_cal.min()))
    macro("emcherospreadafter", "%.0f",
          1e4 * (fv_evt.max() - fv_evt.min()))

    fig, ax = plt.subplots(figsize=(6.9, 3.9))
    x = np.arange(ts.size)
    names = ["today$\\to$" + labels[0]] + [f"{a}$\\to${b}" for a, b in
                                           zip(labels, labels[1:])]
    ax.bar(x - 0.18, fv_cal, width=0.34, color=RUST,
           label="per calendar year")
    ax.bar(x + 0.18, fv_evt, width=0.34, color=TEAL,
           label="per variance year (solved clock)")
    for xi, (_, n_days) in zip(x, events):
        ax.annotate(f"+{n_days:.1f}d", xy=(xi, max(fv_cal[xi], fv_evt[xi])),
                    xytext=(0, 4), textcoords="offset points", ha="center",
                    fontsize=8.5, color=PALETTE["ink"])
    ax.set_xticks(x, names, fontsize=8)
    ax.set_ylabel("forward variance rate")
    ax.set_ylim(0, 0.112)
    ax.legend(fontsize=8.5, loc="upper right")
    save(fig, OUT / "fig_emc_hero.pdf")


# ---------------------------------------------------------------------------
# Figure 2 (parity): the dilated clock, and what it does to the read-off.
# ---------------------------------------------------------------------------

TE, EXTRA = 0.25, 4.0
EVENTS = [(TE, EXTRA)]
SIGMA = 0.20


def figure_clock() -> None:
    T = np.linspace(0.01, 1.0, 400)
    tau = np.array([weighted_variance_years(float(t), EVENTS) for t in T])
    tau_n = np.array([weighted_variance_years(float(t), EVENTS,
                                              normalize=True) for t in T])
    w = SIGMA**2 * tau                      # flat vol on the variance clock
    iv_cal = np.sqrt(w / T)

    t_probe = 0.30
    tau_probe = weighted_variance_years(t_probe, EVENTS)
    macro("emctauprobe", "%.4f", tau_probe)
    macro("emcdropbp", "%.0f",
          1e4 * SIGMA * (1.0 - np.sqrt(t_probe / tau_probe)))

    fig, axes = plt.subplots(1, 2, figsize=WIDE)
    ax = axes[0]
    ax.plot(T, T, color=SLATE, ls=":", lw=1.4, label=r"calendar $\tau=T$")
    ax.plot(T, tau, color=TEAL, lw=1.9, label=r"variance clock $\tau(T)$")
    ax.plot(T, tau_n, color=RUST, ls="--", lw=1.6, label="normalized")
    ax.axvline(TE, color=PALETTE["ink"], lw=0.8, alpha=0.5)
    callout(ax, "jump $N_e/365$", (TE + 0.005, TE + EXTRA / 365.0 + 0.005),
            (0.36, 0.13))
    ax.set_xlabel(r"calendar time $T$ (years)")
    ax.set_ylabel(r"variance years $\tau$")
    ax.legend(fontsize=8.5, loc="upper left")
    label_panel(ax, "A")
    ax = axes[1]
    ax.plot(T, 100 * SIGMA * np.ones_like(T), color=SLATE, ls=":", lw=1.4,
            label=r"clock vol $\sqrt{w/\tau}$ (flat by construction)")
    ax.plot(T, 100 * iv_cal, color=TEAL, lw=1.9,
            label=r"calendar reading $\sqrt{w/T}$")
    ax.axvline(TE, color=PALETTE["ink"], lw=0.8, alpha=0.5)
    ax.set_xlabel(r"calendar time $T$ (years)")
    ax.set_ylabel("ATM implied volatility (%)")
    ax.legend(fontsize=8.5, loc="upper right")
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.30)
    save(fig, OUT / "fig_emc_clock.pdf")


