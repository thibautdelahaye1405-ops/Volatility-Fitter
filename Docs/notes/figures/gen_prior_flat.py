def listing_gate(obs, req, gamma=1.0):
    return np.clip(1.0 - obs / np.maximum(req, 1e-12), 0.0, 1.0) ** gamma


def listing_support(k_quotes, k_legs, b):
    z = (np.asarray(k_legs)[:, None] - np.asarray(k_quotes)[None, :]) / b
    return np.exp(-0.5 * z * z).sum(axis=1)


def listing_basket_precision(coeff, support, eps=1e-9):
    return 1.0 / np.sum(coeff[coeff != 0.0] ** 2 / (support[coeff != 0.0] + eps))


def verify_listings() -> None:
    d = 0.0
    grid = np.linspace(0.0, 3.0, 301)
    for gamma in (0.5, 1.0, 2.0):
        d = max(d, float(np.max(np.abs(listing_gate(grid, 1.0, gamma)
                                       - activation_gap(grid, 1.0, gamma)))))
    # The full pipeline (support -> basket precision -> gate -> budget split)
    # against production, on the jump market where operators are live.
    op, _ = build_operator_prior(syn_prior_w, SYN_TAU, SYN_TAU, SYN_K, None,
                                 SYN_BUDGET, op_set=["ATM", "RR25", "BF25"],
                                 bandwidth=SYN_BW)
    support = listing_support(SYN_K, op.legs_k, SYN_BW)
    gaps = np.array([listing_gate(listing_basket_precision(op.coeff[r], support), 1.0)
                     for r in range(len(op.names))])
    lam = SYN_BUDGET * gaps / gaps.sum()
    prod = {di["operator"]: di for di in op.diagnostics}
    for r, name in enumerate(op.names):
        d = max(d, abs(float(lam[r]) - prod[name]["activeLambda"]))
        pi = listing_basket_precision(op.coeff[r], support)
        d = max(d, abs(pi - prod[name]["obsPrecision"]))
    sci_macro("pflrefagree", d)


