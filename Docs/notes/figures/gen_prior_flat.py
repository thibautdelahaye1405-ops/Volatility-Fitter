def _gate_row(k_quotes: np.ndarray) -> tuple[list, list, list]:
    """Per-operator (name, gap, lambda) under the shipped defaults, incl. the
    gated-off rows and the var-swap rec (production builders)."""
    op, vs = build_operator_prior(prior_w, T, T, k_quotes, None,
                                  0.5 * k_quotes.size, op_set=OP_SET)
    diags = {d["operator"]: d for d in (op.diagnostics if op else [])}
    names = [n for n in OP_SET if n != "VarSwap"] + ["VarSwap"]
    gaps = [diags.get(n, {"gap": 0.0})["gap"] for n in names[:-1]] + [vs.gap]
    lams = [diags.get(n, {"activeLambda": 0.0})["activeLambda"]
            for n in names[:-1]] + [vs.weight]
    return names, gaps, lams


def figure_activation() -> None:
    budget = 0.5 * K_THIN.size  # the shipped 50% budgets
    sg, unmet = build_prior_anchor(prior_w, T, K_THIN, T, budget)
    macro("pflunmet", "%.0f", 100 * unmet)
    K_SPARSE = K_THIN[:: max(K_THIN.size // 4, 1)]
    macro("pflnsparse", "%d", K_SPARSE.size)

    fig, axes = plt.subplots(1, 2, figsize=WIDE,
                             gridspec_kw={"width_ratios": [1.25, 1.0]})
    ax = axes[0]
    ax.axvspan(-BAND, BAND, color="black", alpha=0.05)
    marker, stems, ba = ax.stem(sg.k, sg.weights)
    plt.setp(stems, color=TEAL, linewidth=2.4)
    plt.setp(marker, color=TEAL, markersize=6)
    plt.setp(ba, color=PALETTE["grid"], linewidth=0.8)
    ax.scatter(K_THIN, np.zeros_like(K_THIN), s=12, color="black", zorder=6,
               label="quotes (ATM band)")
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel("strike-anchor weight")
    ax.legend(loc="upper right")
    ax.text(0.0, 0.92 * sg.weights.max(),
            "quoted band:\nzero deficit,\nzero anchors", ha="center", va="top",
            fontsize=9.0, color=SLATE)
    label_panel(ax, "A")

    # Macros for the honesty section: on the real dense/sparse mornings the
    # support proxy closes every vol-operator gate (legs sit ~one bandwidth
    # from the quoted band on this node).
    names, gaps_d, _ = _gate_row(K_THIN)
    _, gaps_s, _ = _gate_row(K_SPARSE)
    short = {"ATM": "atm", "RR25": "rr", "BF25": "bf", "VarSwap": "vs"}
    for n, gd, gs in zip(names, gaps_d, gaps_s):
        macro("pflgapdense" + short[n], "%.2f", gd)
        macro("pflgapsparse" + short[n], "%.2f", gs)

    # Panel B: the gate doing its canonical job, on the jump market of case
    # file 1 (dense ATM quotes, dark wings): ATM off, wing operators on.
    ax = axes[1]
    op, vs = build_operator_prior(syn_prior_w, SYN_TAU, SYN_TAU, SYN_K, None,
                                  SYN_BUDGET, op_set=OP_SET, bandwidth=SYN_BW)
    diags = {d["operator"]: d for d in (op.diagnostics if op else [])}
    gaps = [diags.get(n, {"gap": 0.0})["gap"] for n in OP_SET[:-1]] + [vs.gap]
    lams = [diags.get(n, {"activeLambda": 0.0})["activeLambda"]
            for n in OP_SET[:-1]] + [vs.weight]
    cols = [SLATE if g <= 0 else RUST for g in gaps]
    ax.bar(np.arange(len(OP_SET)), gaps, color=cols, alpha=0.9)
    for i, (g, lam) in enumerate(zip(gaps, lams)):
        note = "off" if g <= 0 else fr"$\lambda={lam:.0f}$"
        ax.text(i, g + 0.04, note, ha="center", fontsize=9, color=PALETTE["ink"])
    ax.set_xticks(np.arange(len(OP_SET)))
    ax.set_xticklabels(OP_SET, fontsize=9.5)
    ax.set_ylim(0, 1.3)
    ax.set_ylabel(r"gate $g$ (0 = off, 1 = full pull)")
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.34)
    save(fig, OUT / "fig_pfl_activation.pdf")
    print("  real dense gaps:", dict(zip(names, np.round(gaps_d, 3))),
          "| real sparse gaps:", dict(zip(names, np.round(gaps_s, 3))),
          "| jump-market gaps:", np.round(gaps, 3))


