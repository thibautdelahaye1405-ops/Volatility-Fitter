def figure_basket() -> None:
    # Coverage sweep on the jump-market smile (tau = 0.5), the case-file
    # configuration: 13 quotes spread over a widening band, kernel b = 0.03.
    tau = SYN_TAU
    halfwidths = np.linspace(0.01, 0.30, 40)
    ops = ["ATM", "RR25", "BF25"]
    obs = {o: [] for o in ops}
    for hw in halfwidths:
        kq = np.linspace(-hw, hw, 13)
        tgt, _ = build_operator_prior(syn_prior_w, tau, tau, kq, None, 1.0,
                                      op_set=ops, required_precision=1e9,
                                      bandwidth=SYN_BW)
        got = {d["operator"]: d["obsPrecision"] for d in tgt.diagnostics}
        for o in ops:
            obs[o].append(got.get(o, np.nan))
    k_p = delta_strike(syn_prior_w, tau, 1.0 - OPERATOR_DELTAS["RR25"])
    macro("pflkptwofive", "%.2f", k_p)
    macro("pflkctwofivereal", "%.2f", delta_strike(prior_w, T, 0.25))
    macro("pflkptwofivereal", "%.2f", delta_strike(prior_w, T, 0.75))

    fig, axes = plt.subplots(1, 2, figsize=WIDE)
    ax = axes[0]
    for o, c in zip(ops, (SLATE, TEAL, AMBER)):
        ax.plot(halfwidths, obs[o], color=c, lw=1.9, label=o)
    ax.axhline(1.0, color=PALETTE["ink"], lw=0.7, ls=":")
    ax.annotate(r"$\pi_{\mathrm{req}}=1$", xy=(0.012, 1.35),
                fontsize=8.5, color=SLATE)
    ax.axvline(abs(k_p), color=AMBER, lw=0.8, ls=":")
    callout(ax, "coverage reaches the\n25$\\Delta$ put leg at $|k_p|$",
            xy=(abs(k_p), 0.3), xytext=(0.17, 0.012))
    ax.set_xlabel("quote coverage half-width (log-moneyness)")
    ax.set_ylabel(r"basket precision $\pi(O)$")
    ax.set_yscale("log")
    ax.set_ylim(1e-4, 3e2)
    ax.legend(loc="upper left")
    label_panel(ax, "A")

    ax = axes[1]
    s_put = np.linspace(0.0, 4.0, 200)
    rr = np.array([_operator_obs_info(np.array([1.0, -1.0]),
                                      np.array([4.0, sp])) for sp in s_put])
    bf = np.array([_operator_obs_info(np.array([0.5, 0.5, -1.0]),
                                      np.array([4.0, sp, 8.0])) for sp in s_put])
    ax.plot(s_put, rr, color=TEAL, lw=1.9, label="RR (call leg support 4)")
    ax.plot(s_put, bf, color=AMBER, lw=1.9, label="BF (call 4, ATM 8)")
    callout(ax, "one dead leg unidentifies\nthe whole basket",
            xy=(0.06, 0.06), xytext=(1.1, 1.25))
    ax.set_xlabel("put-leg quote support $s_p$")
    ax.set_ylabel(r"basket precision $\pi(O)$")
    ax.legend(loc="lower right")
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.34)
    save(fig, OUT / "fig_pfl_basket.pdf")


