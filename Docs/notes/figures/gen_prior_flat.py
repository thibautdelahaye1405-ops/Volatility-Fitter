# Figure 2: the gate (production) and its dead zone vs ridge shrinkage.
# ---------------------------------------------------------------------------


def figure_gate() -> None:
    x = np.linspace(0.0, 2.0, 400)  # observation precision / required
    fig, axes = plt.subplots(1, 2, figsize=WIDE)
    ax = axes[0]
    for gamma, c in ((0.5, AMBER), (1.0, TEAL), (2.0, VIOLET)):
        ax.plot(x, activation_gap(x, 1.0, gamma), color=c,
                label=fr"$\gamma={gamma}$")
    ax.axvline(1.0, color=SLATE, ls=":", lw=1.2)
    ax.annotate(r"$\pi=\pi_{\mathrm{req}}$", xy=(1.06, 0.48), fontsize=9.5,
                color=SLATE)
    ax.set_xlabel(r"observation precision $\pi/\pi_{\mathrm{req}}$")
    ax.set_ylabel(r"gate $g(\pi)$")
    ax.legend(loc="upper right")
    label_panel(ax, "A")

    ax = axes[1]
    lam0 = 0.6  # a visible prior precision
    ridge = lam0 / (lam0 + x)
    gated = lam0 * activation_gap(x, 1.0, 1.0) / (lam0 * activation_gap(x, 1.0, 1.0) + x)
    gated[x <= 0] = 1.0
    ax.plot(x, ridge, color=RUST, ls="--", lw=1.9, label="ridge (fixed prior precision)")
    ax.plot(x, gated, color=TEAL, lw=1.9, label="gated prior")
    ax.axvline(1.0, color=SLATE, ls=":", lw=1.2)
    callout(ax, "the dead zone: bias exactly 0,\nnot merely small",
            xy=(1.45, 0.012), xytext=(0.98, 0.42))
    callout(ax, "ridge still biases a\nwell-observed coordinate",
            xy=(1.6, float(lam0 / (lam0 + 1.6)) + 0.01), xytext=(0.62, 0.68))
    ax.set_xlabel(r"observation precision $\pi/\pi_{\mathrm{req}}$")
    ax.set_ylabel("share of posterior owned by the prior")
    ax.legend(loc="upper right")
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.34)
    save(fig, OUT / "fig_pfl_gate.pdf")


