def figure_flat() -> None:
    kk = np.linspace(-0.42, 0.30, 320)
    fits = []
    for n_order in (4, 6, 8):  # one model family, degrees of freedom varied
        for reg in (0.0, 1e-6):
            f = calibrate_slice(K_THIN, W_THIN, T, n_order=n_order, reg_lambda=reg)
            fits.append(vol(f.slice.implied_w(kk)))
    fits.append(vol(calibrate_svi(K_THIN, W_THIN, T).raw.total_variance(kk)))
    fits.append(vol(calibrate_sigmoid(K_THIN, W_THIN, T, n_cores=2).implied_w(kk)))
    fits = np.array(fits)
    spread = fits.max(axis=0) - fits.min(axis=0)
    i_atm = int(np.argmin(np.abs(kk)))
    i_wing = int(np.argmin(np.abs(kk + 0.30)))
    macro("pflnfits", "%d", fits.shape[0])
    macro("pflspreadatmbp", "%.0f", 100 * spread[i_atm])
    macro("pflspreadwing", "%.1f", spread[i_wing])
    macro("pflnthin", "%d", K_THIN.size)
    macro("pflnout", "%d", K_OUT.size)

    support = _quote_support(K_THIN, None, kk, 0.06)

    fig, axes = plt.subplots(1, 2, figsize=WIDE,
                             gridspec_kw={"width_ratios": [1.3, 1.0]})
    ax = axes[0]
    ax.axvspan(-BAND, BAND, color="black", alpha=0.05)
    for row in fits:
        ax.plot(kk, row, color=TEAL, lw=0.9, alpha=0.45)
    ax.scatter(K_THIN, vol(W_THIN), s=15, color="black", zorder=6,
               label="this morning's quotes")
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel("implied volatility (%)")
    ax.set_ylim(bottom=float(fits.min()) - 1.0)
    ytop = float(fits[:, : i_wing + 1].max()) + 2.5
    ax.set_ylim(top=ytop)
    ax.legend(loc="upper right")
    ybot = float(fits.min()) - 1.0
    callout(ax, "eight fits, one likelihood:\nindistinguishable where quoted",
            xy=(-0.02, float(fits[:, i_atm].mean()) + 0.4),
            xytext=(-0.41, ybot + 0.13 * (ytop - ybot)))
    label_panel(ax, "A")
    ax = axes[1]
    ax.plot(kk, support, color=SLATE, lw=1.8, label="quote support $s(k)$")
    ax.set_ylim(top=1.22 * float(support.max()))
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel("effective quote count", color=SLATE)
    ax2 = ax.twinx()
    ax2.plot(kk, spread, color=RUST, lw=1.8)
    ax2.set_ylabel("ensemble spread (vol pts)", color=RUST)
    ax2.grid(False)
    ax2.spines["top"].set_visible(False)
    ax.legend(loc="upper center")
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.46)
    save(fig, OUT / "fig_pfl_flat.pdf")


