# Figure 4: the coordinates demo — a level jump, quoted only at the money
# (the runnable form of case file 1, production calibrator throughout).
# ---------------------------------------------------------------------------


def base_sigma(k):
    k = np.asarray(k, dtype=float)
    return 0.20 - 0.4 * k + 0.5 * k * k


# The controlled overnight-jump market (the runnable form of case file 1):
# yesterday's shape at yesterday's level; today the level jumps 4 vol points
# and only |k| <= 0.05 is quoted.
SYN_TAU, SYN_JUMP = 0.5, 0.04
SYN_K = np.linspace(-0.05, 0.05, 11)
SYN_BUDGET = 5.0 * SYN_K.size
SYN_BW = 0.03


def syn_prior_w(k):
    s = base_sigma(k)
    return s * s * SYN_TAU


SYN_W_TODAY = (base_sigma(SYN_K) + SYN_JUMP) ** 2 * SYN_TAU


def figure_level() -> None:
    tau, d_level, kw = SYN_TAU, SYN_JUMP, -0.10
    K, w_today, budget = SYN_K, SYN_W_TODAY, SYN_BUDGET
    prior_w_syn = syn_prior_w

    op, _ = build_operator_prior(prior_w_syn, tau, tau, K, None, budget,
                                 op_set=["ATM", "RR25", "BF25"], bandwidth=SYN_BW)
    fac, _ = build_factor_prior(prior_w_syn, tau, tau, K, None, budget,
                                factor_set=["skew", "curvature"], step=0.06,
                                bandwidth=SYN_BW)
    sg, _ = build_prior_anchor(prior_w_syn, tau, K, tau, budget,
                               deltas=(0.05, 0.10, 0.25))
    f_data = calibrate_slice(K, w_today, t=tau, n_order=6)
    f_op = calibrate_slice(K, w_today, t=tau, n_order=6, operator_prior=op)
    f_fac = calibrate_slice(K, w_today, t=tau, n_order=6, operator_prior=fac)
    f_sg = calibrate_slice(K, w_today, t=tau, n_order=6, prior_anchor=sg)

    def v(fit, k):
        return 100.0 * float(np.sqrt(max(fit.slice.implied_w(np.array([k]))[0], 1e-12) / tau))

    true_wing, old_wing = 100 * (base_sigma(kw) + d_level), 100 * base_sigma(kw)
    macro("pflopwingerr", "%.1f", abs(v(f_op, kw) - true_wing))
    macro("pflsgwingerr", "%.1f", abs(v(f_sg, kw) - true_wing))
    macro("pflsgoldgap", "%.1f", abs(v(f_sg, kw) - old_wing))
    kd = -0.20  # deeper in the unquoted wing, between the 10 and 5-delta anchors
    macro("pflopwingerrdeep", "%.1f", abs(v(f_op, kd) - 100 * (base_sigma(kd) + d_level)))
    macro("pflsgwingerrdeep", "%.1f", abs(v(f_sg, kd) - 100 * (base_sigma(kd) + d_level)))
    macro("pflatmopgap", "%.2f", abs(v(f_op, 0.0) - v(f_data, 0.0)))
    macro("pflatmfacgap", "%.2f", abs(v(f_fac, 0.0) - v(f_data, 0.0)))
    macro("pfljump", "%.0f", 100 * d_level)

    kk = np.linspace(-0.28, 0.20, 300)
    fig, ax = plt.subplots(figsize=(6.9, 4.1))
    ax.axvspan(K.min(), K.max(), color="black", alpha=0.05)
    ax.plot(kk, 100 * base_sigma(kk), color=SLATE, ls=":", lw=1.6,
            label="yesterday (the prior)")
    ax.plot(kk, 100 * (base_sigma(kk) + d_level), color=PALETTE["ink"], lw=1.1,
            label="today's truth (level lifted)")
    ax.plot(kk, [v(f_op, float(x)) for x in kk], color=TEAL, lw=2.0,
            label="operator prior (shape rows)")
    ax.plot(kk, [v(f_sg, float(x)) for x in kk], color=RUST, ls="--", lw=1.9,
            label="strike anchor (price rows)")
    ax.scatter(K, 100 * np.sqrt(w_today / tau), s=16, color="black", zorder=6)
    callout(ax, "shape rows ride\ntoday's level",
            xy=(-0.235, float(v(f_op, -0.235)) + 0.25), xytext=(-0.14, 38.1))
    callout(ax, "price rows cling to\nyesterday's absolute wing",
            xy=(-0.26, float(v(f_sg, -0.26)) - 0.3), xytext=(-0.298, 26.8))
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel("implied volatility (%)")
    ax.legend(loc="lower left", fontsize=8.7)
    save(fig, OUT / "fig_pfl_level.pdf")


