def figure_extrap() -> None:
    lqd = calibrate_slice(K_QUOTES, W_QUOTES, T, n_order=6, reg_lambda=1e-6).slice
    svi = calibrate_svi(K_QUOTES, W_QUOTES, T).raw
    siv = calibrate_sigmoid(K_QUOTES, W_QUOTES, T, n_cores=0)

    bL_lqd, bR_lqd = lee_slopes(lqd.params)
    svl, svr = svi.wing_slopes()
    wl_z, wr_z = siv.wing_slopes()
    scale = np.sqrt(siv.t) / siv.sigma_ref
    betas = {"LQD": (bL_lqd, bR_lqd), "SVI": (svl, svr),
             "MCS": (abs(wl_z) * scale, abs(wr_z) * scale)}
    for name, (bl, br) in betas.items():
        macro("wlqbeta%sL" % name, "%.4f", bl)
        macro("wlqbeta%sR" % name, "%.4f", br)
    macro("wlqsvicap", "%.4f", svi.b * (1.0 + abs(svi.rho)))

    fig, ax = plt.subplots(figsize=(6.9, 4.0))
    kk = np.linspace(-0.95, 0.80, 400)
    for name, iw, col, ls in (("LQD", lqd.implied_w, TEAL, "-"),
                              ("SVI", svi.implied_w, RUST, "--"),
                              ("MCS", siv.implied_w, AMBER, ":")):
        ax.plot(kk, iw(kk), color=col, lw=1.9, ls=ls, label=name)
    ax.axvspan(K_QUOTES.min(), K_QUOTES.max(), color="black", alpha=0.05,
               label="fit window")
    ax.scatter(K_QUOTES, W_QUOTES, s=10, color="black", zorder=5)
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel(r"total variance $w(k)$")
    ax.legend(fontsize=8.5, loc="upper right")
    callout(ax, "inside: agreement to vol bps",
            xy=(0.0, float(lqd.implied_w(np.array([0.0]))[0])),
            xytext=(-0.42, 0.075))
    callout(ax, "outside: each model's own law",
            xy=(-0.85, float(svi.implied_w(np.array([-0.85]))[0])),
            xytext=(-0.55, 0.150))
    save(fig, OUT / "fig_wlq_extrap.pdf")


