# Figure 7: the LV deep-put slope knob, and what the var-swap sees.
# ---------------------------------------------------------------------------


def figure_lv() -> None:
    # The a-sensitivity lock's surface: a high-vol two-year single-name where
    # the deep-put region below the last strike vertex is actually reachable.
    xi = np.array([0.70, 0.85, 0.95, 1.00, 1.10, 1.30, 2.20])
    tn = np.array([0.0, 1.0, 2.0])
    theta = 0.16 + 0.30 * (1.0 - xi[None, :]) ** 2 + 0.0 * tn[:, None]
    x_grid = 0.01 * np.arange(221)
    tg = 0.01 * np.arange(201)  # T = 2.0
    surf0 = AffineVarianceSurface(t_nodes=tn, x_nodes=xi, theta=theta)

    slopes = (0.0, 1.5, 5.0)
    cols = (SLATE, TEAL, VIOLET)
    xs = np.linspace(0.25, 1.10, 300)
    fig, axes = plt.subplots(1, 2, figsize=WIDE)
    ax = axes[0]
    vols = []
    for a, col in zip(slopes, cols):
        s = surf0.with_left_extrap_a(float(a))
        nu = s.variance(xs, 1.0)
        ax.plot(xs, nu, color=col, lw=1.9,
                label=rf"slope multiplier $a={a:g}$")
        i_t, _ = solve_varswap_source(s, x_grid, tg)
        vols.append(float(np.sqrt(i_t / 2.0)))
    ax.axvline(xi[0], color=PALETTE["ink"], lw=0.8, ls=":")
    ax.annotate("deepest strike vertex", xy=(xi[0] + 0.012, 0.02),
                fontsize=8.5, color=SLATE, rotation=90, va="bottom")
    ax.set_xlabel(r"spot ratio $x$")
    ax.set_ylabel(r"local variance $\nu(x,\,t=1)$")
    ax.legend(fontsize=8.5, loc="upper right")
    label_panel(ax, "A")
    macro("wlqlvswingbp", "%.0f", 1e4 * (max(vols) - min(vols)))
    ax = axes[1]
    ax.plot(slopes, [100 * v for v in vols], "o-", ms=5, color=TEAL, lw=1.8)
    ax.set_xlabel(r"left-wing slope multiplier $a$")
    ax.set_ylabel("fair var-swap vol at $T=2$ (%)")
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.32)
    save(fig, OUT / "fig_wlq_lv.pdf")


