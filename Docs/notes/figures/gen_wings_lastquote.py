def figure_hero() -> None:
    fixture = REPO / "tmp" / "volfit_surfaces_20260718_1324.json"
    if not fixture.exists():
        raise FileNotFoundError(
            f"standing reference export not found: {fixture} — regenerate via "
            "the app's surfaces export (see memory: reference-live-fixture)")
    doc = json.loads(fixture.read_text(encoding="utf-8"))
    spy = next(tk for tk in doc["tickers"] if tk["ticker"] == "SPY")
    node = spy["nodes"][-1]  # 2027-06-17, the longest expiry
    p = node["lqdParams"]
    beta_l, beta_r = lee_slopes(LQDParams(L=p["L"], R=p["R"], a=np.array(p["a"])))
    macro("wlqherobetaL", "%.3f", beta_l)
    macro("wlqherobetaR", "%.3f", beta_r)
    # The moment exponent the left slope implies, via the inverse Lee map.
    qstar = 1.0 / (2.0 * beta_l) + beta_l / 8.0 - 0.5
    macro("wlqheroqstar", "%.2f", qstar)

    ks = np.array([c["k"] for c in node["curve"]])
    ivs = np.array([c["iv"] for c in node["curve"]])
    cols = node["inputs"]["preparedColumns"]
    prep = np.array(node["inputs"]["prepared"], dtype=float)
    kq = prep[:, cols.index("k")]
    ivq = prep[:, cols.index("ivMid")]
    span = (ks.max() - ks.min())
    extrap = (kq.min() - ks.min()) + (ks.max() - kq.max())
    macro("wlqheroextrapshare", "%.0f", 100 * extrap / span)

    fig, ax = plt.subplots(figsize=(6.9, 3.9))
    ax.axvspan(kq.min(), kq.max(), color=AMBER, alpha=0.14, lw=0,
               label="quoted strikes")
    ax.plot(ks, 100 * ivs, color=TEAL, lw=2.0, label="published fitted curve")
    ax.plot(kq, 100 * ivq, "o", ms=1.8, color=RUST, label="prepared quotes")
    ax.annotate(f"structural slopes $\\beta_L={beta_l:.2f}$, "
                f"$\\beta_R={beta_r:.2f}$\n(from the stored parameters)",
                xy=(0.02, 0.86), xycoords="axes fraction", fontsize=9,
                color=PALETTE["ink"])
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel("implied volatility (%)")
    ax.legend(fontsize=8.5, loc="upper right")
    save(fig, OUT / "fig_wlq_hero.pdf")


