def figure_confine() -> None:
    near_target = RawSVI(a=0.0010, b=0.0860, rho=-0.5, m=0.0583, sigma=0.1010)
    k_n = np.linspace(-0.35, 0.30, 23)
    near = calibrate_svi(k_n, near_target.total_variance(k_n), 0.25).raw
    far = calibrate_svi(K_QUOTES, W_QUOTES, T).raw

    kk = np.linspace(-1.15, 0.95, 500)
    w_n, w_f = near.total_variance(kk), far.total_variance(kk)
    inside = (kk >= k_n.min()) & (kk <= k_n.max())
    macro("wlqphantominside", "%.1f",
          1e4 * max(0.0, float(np.max(w_n[inside] - w_f[inside]))))
    macro("wlqphantomwide", "%.1f",
          1e4 * max(0.0, float(np.max(w_n - w_f))))
    grid_data = variance_floor_grid_from(k_n)
    grid_wide = variance_floor_grid()

    fig, ax = plt.subplots(figsize=(6.9, 4.0))
    ax.axvspan(k_n.min(), k_n.max(), color=AMBER, alpha=0.14, lw=0,
               label="traded range (both expiries)")
    ax.plot(kk, w_f, color=TEAL, lw=2.0, label="far slice $w(k,T_2)$")
    ax.plot(kk, w_n, color=RUST, lw=1.9, ls="--",
            label="near slice $w(k,T_1)$ (steep wings)")
    cross = kk[(w_n > w_f)]
    if cross.size:
        ax.plot(cross, np.interp(cross, kk, w_f), color=VIOLET, lw=4.0,
                alpha=0.45, solid_capstyle="butt",
                label="phantom inversion (extrapolation only)")
    ax.plot(grid_data, np.full_like(grid_data, -0.006), "|", ms=7, color=TEAL,
            label="confined floor grid")
    ax.plot(grid_wide, np.full_like(grid_wide, -0.013), "|", ms=7, color=SLATE,
            alpha=0.5, label="wide grid $[-1,1]$")
    ax.set_ylim(-0.02, None)
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel(r"total variance $w(k)$")
    ax.legend(fontsize=8, loc="upper right")
    save(fig, OUT / "fig_wlq_confine.pdf")


