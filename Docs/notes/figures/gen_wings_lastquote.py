def listing_lee_psi(p):
    p = np.asarray(p, dtype=float)
    with np.errstate(divide="ignore"):
        return 2.0 - 4.0 / (np.sqrt(1.0 + 1.0 / p) + 1.0)


def listing_lee_slopes(a_l, a_r):
    beta_l = float(listing_lee_psi(1.0 / a_l)) if a_l > 0.0 else 0.0
    if a_r >= 1.0:
        beta_r = 2.0
    elif a_r > 0.0:
        beta_r = float(listing_lee_psi(1.0 / a_r - 1.0))
    else:
        beta_r = 0.0
    return beta_l, beta_r


def verify_listings() -> None:
    from volfit.models.lqd.basis import endpoint_scales
    lqd = calibrate_slice(K_QUOTES, W_QUOTES, T, n_order=6,
                          reg_lambda=1e-6).slice
    d_psi = float(np.max(np.abs(listing_lee_psi(np.linspace(1e-4, 50, 999))
                                - lee_psi(np.linspace(1e-4, 50, 999)))))
    a_l, a_r = endpoint_scales(lqd.params)
    d_sl = float(np.max(np.abs(np.array(listing_lee_slopes(a_l, a_r))
                               - np.array(lee_slopes(lqd.params)))))
    # Naive-vs-stable psi: the cancellation at extreme p.
    p_big = 1e16
    naive = 2.0 - 4.0 * (np.sqrt(p_big * p_big + p_big) - p_big)
    macro("wlqnaivepsi", "%.1f", naive)
    macro("wlqstablepsi", "%.1f", float(lee_psi(p_big)))
    sci_macro("wlqrefagree", max(d_psi, d_sl, 1e-17))


