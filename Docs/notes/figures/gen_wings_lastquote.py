# A milder cousin of the production test suite's synthetic arb slice (same
# base, same hat placement, amplitude -0.10 instead of -0.90): the variance
# stays strictly positive everywhere -- no floored quotes -- yet the hat's
# CURVATURE still drives the Durrleman density negative at finite z while
