def macro(name: str, fmt: str, value) -> None:
    MACROS.append((r"\newcommand{\%s}{" + fmt + "}") % (name, value))
    NUMBERS[name] = float(value) if isinstance(value, (int, float, np.floating)) else value


def sci_macro(name: str, value: float) -> None:
    """Emit a tiny value as \\ensuremath{m\\times10^{e}} (house number format)."""
    e = int(np.floor(np.log10(abs(value))))
    m = value / 10.0**e
    MACROS.append(r"\newcommand{\%s}{\ensuremath{%.1f\times10^{%d}}}"
                  % (name, m, e))
    NUMBERS[name] = float(value)


def siv_g(model: MultiCoreSiv, z: np.ndarray) -> np.ndarray:
    """Durrleman g of a sigmoid slice via the production analytic kernels."""
    v, vz, vzz = model.variance_z(z)
    return gatheral_g_from_z(z, v, vz, vzz, model.t, model.sigma_ref)


