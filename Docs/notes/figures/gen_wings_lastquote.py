def figure_hinge() -> None:
    off = calibrate_sigmoid(K_ARB, W_ARB, 0.25, n_cores=2, wing_penalty=0.0)
    on = calibrate_sigmoid(K_ARB, W_ARB, 0.25, n_cores=2, wing_penalty=1e3)
    z_q = K_ARB / (off.sigma_ref * np.sqrt(off.t))
    z = np.linspace(z_q.min() - 2.0, z_q.max() + 2.0, 400)
    g_off, g_on = siv_g(off, z), siv_g(on, z)
    macro("wlqarbgraw", "%.1f", float(np.nanmin(g_off)))
    macro("wlqarbgfixed", "%.3f", float(np.nanmin(g_on)))

    # Byte-identity on the clean benchmark slice.
    clean_off = calibrate_sigmoid(K_QUOTES, W_QUOTES, T, n_cores=2,
                                  wing_penalty=0.0)
    clean_on = calibrate_sigmoid(K_QUOTES, W_QUOTES, T, n_cores=2,
                                 wing_penalty=1e3)
    kk = np.linspace(-0.5, 0.45, 201)
    diff = float(np.max(np.abs(clean_on.implied_w(kk) - clean_off.implied_w(kk))))
    sci_macro("wlqbyteid", max(diff, 1e-17))

    fig, axes = plt.subplots(1, 2, figsize=WIDE)
    ax = axes[0]
    kk2 = np.linspace(-0.42, 0.36, 300)
    ax.plot(kk2, 1e2 * np.sqrt(off.implied_w(kk2) / off.t), color=RUST,
            ls="--", lw=1.8, label="penalty off")
    ax.plot(kk2, 1e2 * np.sqrt(on.implied_w(kk2) / on.t), color=TEAL, lw=1.9,
            label="penalty on")
    ax.plot(K_ARB, 1e2 * np.sqrt(W_ARB / 0.25), "o", ms=3.0, color="black",
            zorder=5, label="quotes")
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel("implied volatility (%)")
    ax.legend(fontsize=8.5, loc="upper right")
    label_panel(ax, "A")
    ax = axes[1]
    ax.axvspan(z.min(), z_q.min(), color=AMBER, alpha=0.14, lw=0)
    ax.axvspan(z_q.max(), z.max(), color=AMBER, alpha=0.14, lw=0)
    ax.plot(z, g_off, color=RUST, ls="--", lw=1.8, label="penalty off")
    ax.plot(z, g_on, color=TEAL, lw=1.9, label="penalty on")
    ax.axhline(0.0, color=PALETTE["ink"], lw=0.8, ls=":")
    ax.annotate("hinge grid extends 2 units\npast the traded range",
                xy=(0.985, 0.05), xycoords="axes fraction", ha="right",
                fontsize=8.5, color=SLATE)
    ax.set_xlabel(r"standardized moneyness $z$")
    ax.set_ylabel(r"Durrleman $g(z)$")
    ax.legend(fontsize=8.5, loc="center left")
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.30)
    save(fig, OUT / "fig_wlq_hinge.pdf")


