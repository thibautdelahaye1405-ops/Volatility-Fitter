"""Figure suite for the missing-derivative edition of Note 12 (spot-vol).

Run from the repository root with the project virtual environment::

    .venv\\Scripts\\python.exe Docs\\notes\\figures\\gen_ssr_derivative.py

Every transport, delta and repriced smile is produced by PRODUCTION machinery
(``volfit.dynamics.{ssr,transport}``, the Black core, and the affine Dupire
solver); local code only wraps the real SPY curve of the standing export
``tmp/volfit_surfaces_20260718_1324.json`` as callables and differencing.

Outputs, written next to this script:

* ``fig_smd_fan.pdf``     -- the fan of tomorrows: the real SPY smile
                             transported in the three regimes + ATM response;
* ``fig_smd_signs.pdf``   -- the two signs: curve rule (k + h) vs quote rule
                             (k - h) under one up-move;
* ``fig_smd_delta.pdf``   -- the stakes: total smile delta per regime;
* ``fig_smd_exact.pdf``   -- exact sticky-LV (Hagan map) vs the linear
                             transport: wing gap and quadratic error;
* ``fig_smd_reprice.pdf`` -- SSR as an output: realized SSR of the exact
                             frozen-grid Dupire reprice across maturities;
* ``ssr_derivative_tables.tex`` / ``ssr_derivative_numbers.json``.
"""

