# Appendix D verification: the two listings against production.
# ---------------------------------------------------------------------------


def listing_implied_forward(K, call_mid, put_mid):
    y = call_mid - put_mid
    b, a = np.polyfit(K, y, 1)
    D = -b
    return a / D, D


def listing_clamp_forward(K, call_mid, put_mid, t, F, D, r_lo=-0.05, r_hi=0.30):
    r = -np.log(D) / t
    if r_lo <= r <= r_hi:
        return F, D
    D = np.exp(-np.clip(r, r_lo, r_hi) * t)
    F = np.mean(K + (call_mid - put_mid) / D)
    return F, D


def verify_listings() -> None:
    c, p = euro_prices(STRIKES)
    snap = make_chain(STRIKES, c, p, round_cents=False)
    res = implied_forward(snap, EXPIRY)
    f_l, d_l = listing_implied_forward(STRIKES, c, p)
    agree = max(abs(f_l - res.forward), abs(d_l - res.discount), 1e-16)
    macro("fwdinfrefagree", "%.0f", np.floor(np.log10(agree)))

    # A tilted board: put mids drift linearly in strike -> the slope (only)
    # is corrupted, hard enough that the implied rate leaves the physical
    # band; production clamps the rate and re-derives the level.
    tilt = 0.055 * (STRIKES - STRIKES.mean())
    snap_bad = make_chain(STRIKES, c, p + tilt, round_cents=False)
    res_bad = implied_forward(snap_bad, EXPIRY, reference_date=REF)
    r_bad = -np.log(res_bad.discount) / T
    f_ref, d_ref = listing_implied_forward(STRIKES, c, p + tilt)
    f_cl, d_cl = listing_clamp_forward(STRIKES, c, p + tilt, T, f_ref, d_ref)
    macro("fwdinfrefclrate", "%.1f", 100 * r_bad)
    macro("fwdinfrefclrateeq", "%.1e", abs(-np.log(d_cl) / T - r_bad))
    macro("fwdinfrefclfbp", "%.0f", 1e4 * abs(f_cl - res_bad.forward) / res_bad.forward)
    macro("fwdinfrefrawrate", "%.1f", -100 * np.log(d_ref) / T)


