def listing_operator(nodes, factors):
    n = len(nodes)
    Q = np.zeros((n, n))
    for i, j, p, b in factors:
        u = np.zeros(n)
        u[i] = 1.0
        u[j] = -b
        Q += p * np.outer(u, u)
    return Q


def listing_solve(Q, kappa, obs, d, r):
    Qp = Q + np.diag(kappa)
    b = np.zeros(len(Q))
    Qp[obs, obs] += r
    b[obs] += r * d
    sigma = np.linalg.inv(Qp)
    return sigma @ b, sigma


def verify_listings() -> None:
    nodes = ["A", "B", "C", "D"]
    edges = [message_edge("B", "A", 800.0, 1.3), message_edge("C", "B", 500.0, 0.8),
             message_edge("D", "C", 300.0, 1.1), message_edge("D", "A", 200.0, 0.9)]
    op = build_message_operator(nodes, edges)
    kappa = anchor_precisions(nodes, edges, {"custom": 0.5})
    obs = np.array([0])
    d = np.array([1.0])
    r = np.array([1e6])
    post = message_posterior_update(op, obs, d, r, anchor_precision=kappa)
    Q_ref = listing_operator(nodes, op.factors)
    mean_ref, sigma_ref = listing_solve(Q_ref, kappa, obs, d, r)
    dev = max(
        float(np.max(np.abs(Q_ref - op.q_matrix))),
        float(np.max(np.abs(mean_ref - post.mean))),
        float(np.max(np.abs(np.diag(sigma_ref) - post.marginal_variance))),
    )
    # cycle gauge cross-check: consistent potentials flag nothing, an
    # inconsistent triangle flags its closing edge product.
    good = [message_edge("B", "A", 1.0, 2.0), message_edge("C", "B", 1.0, 3.0),
            message_edge("C", "A", 1.0, 6.0)]
    bad = [message_edge("B", "A", 1.0, 2.0), message_edge("C", "B", 1.0, 3.0),
           message_edge("C", "A", 1.0, 5.0)]
    flags_good = cycle_beta_products(["A", "B", "C"], good)
    flags_bad = cycle_beta_products(["A", "B", "C"], bad)
    macro("gmcycleflags", "%d", len(flags_good))
    macro("gmcyclebad", "%.3f", flags_bad[0].product if flags_bad else float("nan"))
    sci_macro("gmrefagree", dev)


