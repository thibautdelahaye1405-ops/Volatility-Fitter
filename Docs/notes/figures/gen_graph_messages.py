# Figure 5: multi-hop — the mean pays no toll, the variance accumulates.
# ---------------------------------------------------------------------------


def figure_hops() -> None:
    n_hops = 6
    p = 1.0e3
    beta = 1.0
    nodes = [f"N{i}" for i in range(n_hops + 1)]
    edges = [message_edge(f"N{i+1}", f"N{i}", p, beta) for i in range(n_hops)]
    _, post = solve(nodes, edges, ["N0"], [1.0])
    means = post.mean
    sds = np.sqrt(post.marginal_variance)
    hops = np.arange(n_hops + 1)
    predicted_sd = np.sqrt(hops / p)  # §13 accumulation at beta=1, clamped source
    macro("gmhopmean", "%.3f", means[-1])
    macro("gmhopsdgap", "%.4f", float(np.max(np.abs(sds[1:] - predicted_sd[1:]))))

    fig, ax = plt.subplots(figsize=(6.9, 3.8))
    ax.plot(hops, means, "o-", ms=5, color=TEAL, lw=2.0,
            label=r"posterior mean $\widehat z$")
    ax.fill_between(hops, means - 1.96 * sds, means + 1.96 * sds, color=TEAL,
                    alpha=0.14, label=r"95% band ($\pm1.96\,$sd)")
    ax.plot(hops, means + 1.96 * predicted_sd, color=SLATE, lw=1.1, ls=":",
            label=r"§13 accumulation $\sqrt{k/p}$ (exact)")
    ax.plot(hops, means - 1.96 * predicted_sd, color=SLATE, lw=1.1, ls=":")
    callout(ax, "the mean crosses six hops undamped;\nconfidence pays the toll",
            xy=(5.2, means[-1] + 0.005), xytext=(2.7, 0.878))
    ax.set_xlabel("hops from the lit source (chain, $\\beta=1$)")
    ax.set_ylabel("posterior innovation (vol pts)")
    ax.legend(loc="lower left", fontsize=8.6)
    save(fig, OUT / "fig_gm_hops.pdf")


