def figure_amplitude() -> None:
    study = json.loads((RESULTS / "message_phase0.json").read_text())
    corr = study["corroboration"]
    rho = corr["indexOnly"]["b"]
    measured = {1: corr["indexOnly"]["b"], 2: corr["combinedEqualWeight"]["b"]}
    macro("gmrhoindex", "%.3f", rho)
    macro("gmrhopeer", "%.3f", corr["peersOnly"]["b"])
    macro("gmtwosource", "%.3f", measured[2])
    macro("gmuplift", "%.0f", 100 * corr["uplift"])
    macro("gmupliftbar", "%.0f", 100 * corr["upliftBar"])
    macro("gmphasen", "%d", corr["indexOnly"]["n"])

    ks = np.arange(1, 7)
    transfer = []
    for k in ks:
        nodes = ["X"] + [f"S{i}" for i in range(k)]
        edges = [message_edge("X", f"S{i}", 1e3, 1.0, "broad_index")
                 for i in range(k)]
        kappa = anchor_precisions(nodes, edges, {"broad_index": rho})
        _, post = solve(nodes, edges, [f"S{i}" for i in range(k)],
                        np.ones(k), kappa=kappa)
        transfer.append(post.mean[0])
    transfer = np.array(transfer)
    pred2 = transfer[1]
    macro("gmpredtwo", "%.3f", pred2)
    macro("gmpredgap", "%.1f", 100 * abs(pred2 - measured[2]) / measured[2])

    fig, ax = plt.subplots(figsize=(6.9, 4.0))
    ax.plot(ks, transfer, "o-", ms=6, color=TEAL, lw=2.0,
            label=r"node-linked anchor (production): $k\rho/(1-\rho+k\rho)$")
    ax.axhline(rho, color=RUST, lw=1.7, ls="--",
               label="edge-linked alternative (constant transfer)")
    ax.scatter([1, 2], [measured[1], measured[2]], s=90, marker="D",
               color=PALETTE["ink"], zorder=6,
               label="measured on stored rows (Phase 0)")
    callout(ax,
            "calibrated on the one-source slope alone,\n"
            f"the anchor predicts {pred2:.3f} for two sources;\n"
            f"measured: {measured[2]:.3f} — zero free parameters",
            xy=(2.02, measured[2]), xytext=(2.6, 0.44))
    ax.set_xlabel("number of equal corroborating lit sources $k$")
    ax.set_ylabel("effective transfer per unit message")
    ax.set_ylim(0.30, 0.85)
    ax.legend(loc="upper left", fontsize=8.6)
    save(fig, OUT / "fig_gm_amplitude.pdf")


