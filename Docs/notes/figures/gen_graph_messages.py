def _row_form_transfer(p_lit: float, p_dead: float, anchor: float = 1e-6) -> float:
    """The spec's REJECTED row-normalized operator on the dead-informer
    fixture, solved exactly (local reference, not production): receiver R fed
    by lit L and dead D, energy q_R (z_R - (p_lit z_L + p_dead z_D)/q_R)^2
    with q_R = p_lit + p_dead, plus a small anchor on every node."""
    q = p_lit + p_dead
    nodes = ["R", "L", "D"]
    n = 3
    u = np.array([1.0, -p_lit / q, -p_dead / q])
    Q = q * np.outer(u, u) + anchor * np.eye(n)
    r = CLAMP
    Q[1, 1] += r
    b = np.zeros(n)
    b[1] = r * 1.0
    return float(np.linalg.solve(Q, b)[0])


def figure_accountant() -> None:
    ratios = np.geomspace(0.1, 100.0, 40)
    p_lit = 1.0e3
    pairwise, rowform = [], []
    for ratio in ratios:
        edges = [
            message_edge("R", "L", p_lit, 1.0),
            message_edge("R", "D", p_lit * ratio, 1.0),
        ]
        _, post = solve(["R", "L", "D"], edges, ["L"], [1.0])
        pairwise.append(post.mean[0])
        rowform.append(_row_form_transfer(p_lit, p_lit * ratio))
    i_eq = int(np.argmin(np.abs(ratios - 1.0)))
    macro("gmdeadhalf", "%.2f", rowform[i_eq])
    macro("gmpairflat", "%.2f", min(pairwise))

    # Repeated path: T hears A directly and through M, and A is observed at
    # FINITE precision r = p (the golden fixture); naive accounting treats the
    # two routes as independent messages with the §12 effective precisions.
    p = 1.0e3
    edges = [
        message_edge("T", "A", p, 1.0),
        message_edge("M", "A", p, 1.0),
        message_edge("T", "M", p, 1.0),
    ]
    _, post = solve(["T", "A", "M"], edges, ["A"], [1.0], r=p)
    var_global = float(post.marginal_variance[0])
    v_a = 1.0 / p                       # source posterior variance at r = p
    var_direct = v_a + 1.0 / p          # §12 effective message variance
    var_path = v_a + 1.0 / p + 1.0 / p  # two relation legs
    var_naive = 1.0 / (1.0 / var_direct + 1.0 / var_path)
    macro("gmrepeatvar", "%.2f", var_global * p)
    macro("gmnaivevar", "%.2f", var_naive * p)
    macro("gmrepeatratio", "%.2f", var_global / var_naive)

    fig, axes = plt.subplots(1, 2, figsize=WIDE,
                             gridspec_kw={"width_ratios": [1.25, 1.0]})
    ax = axes[0]
    ax.semilogx(ratios, pairwise, color=TEAL, lw=2.2,
                label="pairwise factors (production)")
    ax.semilogx(ratios, rowform, color=RUST, lw=2.0, ls="--",
                label="row-normalized alternative")
    ax.set_ylim(-0.05, 1.15)
    ax.set_xlabel(r"dead informer's configured precision $p_{\mathrm{dead}}/p_{\mathrm{lit}}$")
    ax.set_ylabel(r"receiver transfer $\widehat z_R / z_L$")
    callout(ax, "a dead informer dilutes the row\nform toward zero; the pairwise\nfactor marginalizes it away exactly",
            xy=(20.0, rowform[-8] + 0.03), xytext=(0.14, 0.36))
    ax.legend(loc="lower left", fontsize=8.7)
    label_panel(ax, "A")
    ax = axes[1]
    bars = [1e3 * var_global, 1e3 * var_naive]
    ax.bar([0, 1], bars, width=0.55, color=[TEAL, RUST])
    ax.set_xticks([0, 1], ["global solve\n(production)", "naive message\naccounting"])
    ax.set_ylabel(r"posterior variance at T ($\times p^{-1}$)")
    for i, v in enumerate(bars):
        ax.text(i, v + 0.04, f"{v:.2f}/p", ha="center", fontsize=9.5)
    ax.set_ylim(0, 2.2)
    callout(ax, "two routes, one source: treating\nthem as independent messages\noverstates precision by "
                f"{var_global / var_naive:.0%}$-$1",
            xy=(1.0, bars[1] + 0.06), xytext=(0.02, 1.9))
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.34)
    save(fig, OUT / "fig_gm_accountant.pdf")


