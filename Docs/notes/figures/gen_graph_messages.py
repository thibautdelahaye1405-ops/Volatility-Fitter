def figure_compete() -> None:
    nodes = ["6M", "3M", "1Y"]
    cases = []
    # (a) equal precision, beta one: cancel.
    edges = [message_edge("6M", "3M", 1e3, 1.0),
             message_edge("6M", "1Y", 1e3, 1.0)]
    _, post = solve(nodes, edges, ["3M", "1Y"], [-1.0, 1.0])
    cases.append(("equal $p$, $\\beta=1$", post.mean[0]))
    # (b) 3M at 3p: outvoted.
    edges = [message_edge("6M", "3M", 3e3, 1.0),
             message_edge("6M", "1Y", 1e3, 1.0)]
    _, post = solve(nodes, edges, ["3M", "1Y"], [-1.0, 1.0])
    cases.append(("$p_{3M}=3p$", post.mean[0]))
    # (c) calendar betas map the signals into receiver units first.
    edges = [message_edge("6M", "3M", 1e3, calendar_beta(0.5, 0.25)),
             message_edge("6M", "1Y", 1e3, calendar_beta(0.5, 1.0))]
    _, post = solve(nodes, edges, ["3M", "1Y"], [-1.0, 1.0])
    cases.append((r"$\alpha_T=1$ betas", post.mean[0]))
    macro("gmcompetecancel", "%.2f", cases[0][1])
    macro("gmcompeteout", "%.2f", cases[1][1])
    macro("gmcompetebeta", "%.2f", cases[2][1])

    fig, ax = plt.subplots(figsize=(6.9, 3.6))
    x = np.arange(3)
    vals = [c[1] for c in cases]
    ax.bar(x, vals, width=0.5, color=[SLATE, AMBER, TEAL])
    ax.axhline(0.0, color=PALETTE["ink"], lw=0.8)
    ax.set_xticks(x, [c[0] for c in cases])
    ax.set_ylabel(r"$\widehat z_{6M}$ (vol pts)")
    for i, v in enumerate(vals):
        ax.text(i, v + (0.05 if v >= 0 else -0.12), f"{v:+.2f}", ha="center",
                fontsize=10)
    ax.set_ylim(-0.8, 1.1)
    callout(ax, "same raw signals $\\mp1$: the betas map\nthem into 6M units before the vote",
            xy=(1.82, vals[2] + 0.02), xytext=(-0.38, 0.82))
    save(fig, OUT / "fig_gm_compete.pdf")


