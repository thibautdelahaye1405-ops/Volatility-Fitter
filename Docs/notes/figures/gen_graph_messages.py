def macro(name: str, fmt: str, value) -> None:
    MACROS.append((r"\newcommand{\%s}{" + fmt + "}") % (name, value))
    NUMBERS[name] = float(value) if isinstance(value, (int, float, np.floating)) else value


def sci_macro(name: str, value: float) -> None:
    v = max(abs(value), 1e-17)
    e = int(np.floor(np.log10(v)))
    MACROS.append(r"\newcommand{\%s}{\ensuremath{%.1f\times10^{%d}}}"
                  % (name, v / 10.0**e, e))
    NUMBERS[name] = float(value)


def solve(nodes, edges, lit, z_lit, r=CLAMP, kappa=None, handle=0):
    """Production operator + posterior for one handle, informers clamped."""
    op = build_message_operator(nodes, edges, handle=handle)
    obs = np.array([nodes.index(n) for n in lit])
    return op, message_posterior_update(
        op, obs, np.asarray(z_lit, dtype=float),
        np.full(obs.size, float(r)), anchor_precision=kappa,
    )


