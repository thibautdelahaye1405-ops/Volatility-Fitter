def pipeline_iv(is_call: bool, a_mkt: float, s: float, k: float, t: float,
                r: float, q: float, n: int) -> tuple[float, float]:
    """The full de-Am arithmetic at depth ``n`` on one quote, exactly as the
    pipeline composes it: root sigma*, model EEP at sigma*, subtract, invert
    the shifted price through the analytic Black formula.  Returns
    (sigma*, reported iv)."""
    f = s * float(np.exp((r - q) * t))
    d = float(np.exp(-r * t))
    sig = deamericanize(is_call, a_mkt, s, k, t, r, q, n_steps=n)
    e_n = binomial_price(is_call, s, k, t, sig, r, q, n_steps=n, american=False)
    eep = max(a_mkt - e_n, 0.0)
    logm = float(np.log(k / f))
    shift = 0.0 if is_call else 1.0 - float(np.exp(logm))
    w = implied_total_variance(np.array([logm]),
                               np.array([(a_mkt - eep) / (d * f) + shift]))[0]
    return sig, float(np.sqrt(w / t))


def figure_cancel() -> None:
    k = 90.0  # the worked-example strike (ITM call, dividend magnet)
    depths = np.array([24, 32, 48, 64, 96, 128, 192, 256, 384, 501, 768, 1024])
    n_ref = 4001
    a_ref = binomial_price(True, S0, k, T0, SIG0, R0, Q0, n_steps=n_ref,
                           american=True)
    root_bp, iv_bp = [], []
    for n in depths:
        sig, iv = pipeline_iv(True, a_ref, S0, k, T0, R0, Q0, int(n))
        root_bp.append(1e4 * abs(sig - SIG0))
        iv_bp.append(1e4 * abs(iv - SIG0))
    root_bp, iv_bp = np.array(root_bp), np.array(iv_bp)
    i192 = int(np.flatnonzero(depths == 192)[0])
    i501 = int(np.flatnonzero(depths == 501)[0])
    macro("deamstdepthbp", "%.1f", iv_bp[i192])
    macro("deamstdepthbpscalar", "%.2f", iv_bp[i501])
    macro("deamstrootbp", "%.1f", root_bp[i192])
    NUMBERS["depth_arrays"] = {"depths": depths.tolist(),
                               "root_bp": root_bp.tolist(), "iv_bp": iv_bp.tolist()}

    # Panel B: the same end-to-end error across the whole strike range,
    # aggregated per depth (batch 192, its rejected cheaper cut 128, scalar).
    strikes = np.linspace(88, 120, 17)
    depth_grid = (96, 128, 192, 256, 384, 501)
    med, mx = [], []
    for n in depth_grid:
        errs = []
        for kk in strikes:
            a_m = binomial_price(True, S0, float(kk), T0, SIG0, R0, Q0,
                                 n_steps=n_ref, american=True)
            _, iv = pipeline_iv(True, a_m, S0, float(kk), T0, R0, Q0, n)
            errs.append(1e4 * abs(iv - SIG0))
        errs = np.array(errs)
