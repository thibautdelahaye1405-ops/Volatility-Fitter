def figure_escrow() -> None:
    ref = date(2026, 6, 25)
    r, t = 0.04, 0.75
    model = DividendModel(mode="discrete_absolute",
                          dividends=(Dividend(ref + timedelta(days=128), 3.0),))
    tau_ex = 128 / 365.0
    f_theo = theoretical_forward(S0, r, t, model, ref)
    # A market forward 15 bp of carry under the forecast's: the cash amounts bend.
    f_mkt = f_theo * float(np.exp(-0.0015 * t))
    sched = forward_consistent_cash_schedule(S0, f_mkt, r, t, model, ref)
    assert sched is not None
    div_t, div_a = sched
    macro("deamstalpha", "%.3f", div_a[0] / 3.0)
    q_eq = equivalent_yield(S0, f_mkt, r, t)

    strikes = np.linspace(70.0, 115.0, 24)

    def eep_curve(**kw) -> np.ndarray:
        am = np.array([binomial_price(True, S0, float(k), t, SIG0, r,
                                      american=True, **kw) for k in strikes])
        eu = np.array([binomial_price(True, S0, float(k), t, SIG0, r,
                                      american=False, **kw) for k in strikes])
        return am - eu

    eep_cash = eep_curve(q=0.0, div_times=div_t, div_amounts=div_a)
    eep_yield = eep_curve(q=q_eq)
    deep = eep_yield > 0.05
    macro("deamstescrowratio", "%.1f", float(np.max(eep_cash[deep] / eep_yield[deep])))

    mats = np.linspace(0.08, t, 28)
    k_itm = 90.0
    e_cash, e_yield = [], []
    for tm in mats:
        f_m = theoretical_forward(S0, r, float(tm), model, ref)
        sch = forward_consistent_cash_schedule(S0, f_m, r, float(tm), model, ref)
        kw = ({"q": 0.0, "div_times": sch[0], "div_amounts": sch[1]}
              if sch is not None else {"q": 0.0})
        am = binomial_price(True, S0, k_itm, float(tm), SIG0, r, american=True, **kw)
        eu = binomial_price(True, S0, k_itm, float(tm), SIG0, r, american=False, **kw)
        e_cash.append(am - eu)
        qe = equivalent_yield(S0, f_m, r, float(tm))
        am = binomial_price(True, S0, k_itm, float(tm), SIG0, r, q=qe, american=True)
        eu = binomial_price(True, S0, k_itm, float(tm), SIG0, r, q=qe, american=False)
        e_yield.append(am - eu)

    fig, axes = plt.subplots(1, 2, figsize=WIDE)
    ax = axes[0]
    ax.plot(strikes, eep_cash, color=TEAL, label="discrete cash schedule ($q=0$)")
    ax.plot(strikes, eep_yield, color=AMBER, ls="--", label="continuous-yield fallback")
    ax.set_xlabel("strike $K$")
    ax.set_ylabel(r"$\pi = A - E$ (dollars)")
    ax.legend(loc="upper right", fontsize=9)
    label_panel(ax, "A")
    ax = axes[1]
    ax.plot(mats, e_cash, color=TEAL, label="discrete cash schedule")
    ax.plot(mats, e_yield, color=AMBER, ls="--", label="continuous-yield fallback")
    ax.axvline(tau_ex, color=SLATE, ls=":", lw=1.1)
    ax.annotate("ex-date", xy=(tau_ex, 0.04), xycoords=("data", "axes fraction"),
                xytext=(-42, 0), textcoords="offset points", color=SLATE, fontsize=9)
    ax.set_xlabel("expiry $t$ (years)")
    ax.set_ylabel(r"$\pi$ at $K=90$ (dollars)")
    ax.set_ylim(-0.08, 2.55)
    ax.legend(loc="upper right", fontsize=9)
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.3)
    save(fig, OUT / "fig_deamst_escrow.pdf")


