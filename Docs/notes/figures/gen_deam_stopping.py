def figure_bias_and_vega() -> None:
    strikes = np.linspace(88, 120, 17)
    f = S0 * float(np.exp((R0 - Q0) * T0))
    d = float(np.exp(-R0 * T0))
    am = np.array([binomial_price(True, S0, float(k), T0, SIG0, R0, Q0,
                                  american=True) for k in strikes])
    eu = np.array([binomial_price(True, S0, float(k), T0, SIG0, R0, Q0,
                                  american=False) for k in strikes])
    eep = am - eu
    naive = naive_iv_calls(strikes, am, f, d, T0)
    deam = np.array([deamericanize(True, float(a), S0, float(k), T0, R0, Q0)
                     for a, k in zip(am, strikes)])
    bias_bp = 1e4 * (naive - deam)
    macro("deamstmaxbias", "%.0f", np.nanmax(bias_bp))

    fig, axes = plt.subplots(1, 2, figsize=WIDE)
    ax = axes[0]
    ax.axhline(100 * SIG0, color=SLATE, ls=":", lw=1.2)
    ax.plot(strikes, 100 * naive, color=RUST, label="American price, European inversion")
    ax.plot(strikes, 100 * deam, color=TEAL, ls="--", label=r"de-Americanized $\sigma^*$")
    ax.annotate("true vol 25%", xy=(91.0, 100 * SIG0),
                xytext=(6, -14), textcoords="offset points", color=SLATE, fontsize=9)
    ax.set_xlabel("strike $K$")
    ax.set_ylabel("implied volatility (%)")
    ax.set_ylim(24.0, 28.4)
    ax.legend(loc="upper right", fontsize=9)
    label_panel(ax, "A")
    ax = axes[1]
    ax.plot(strikes, eep, color=TEAL)
    ax.fill_between(strikes, eep, color=TEAL, alpha=0.12)
    ax.set_xlabel("strike $K$")
    ax.set_ylabel(r"$\pi = A - E$ (dollars)")
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.3)
    save(fig, OUT / "fig_deamst_bias.pdf")

    # The dictionary: a price nuisance pi moves implied vol by ~ pi / vega.
    vega = black_vega_sigma(np.log(strikes / f), deam, T0)  # normalized-call vega
    pred_bp = 1e4 * (eep / (d * f)) / vega
