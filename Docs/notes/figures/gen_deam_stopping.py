def figure_map() -> None:
    strikes = np.linspace(60.0, 145.0, 52)
    mats = np.linspace(0.04, 1.5, 38)
    KK, TT = np.meshgrid(100.0 * strikes / S0, mats)

    def panel(is_call: bool, r: float, q: float):
        eep = np.empty((mats.size, strikes.size))
        pinned = np.empty_like(eep, dtype=bool)
        flags = np.full(strikes.size, is_call)
        sig = np.full(strikes.size, SIG0)
        intrinsic = np.maximum(S0 - strikes, 0.0) if is_call else np.maximum(strikes - S0, 0.0)
        for i, t in enumerate(mats):
            am = binomial_price_batch(flags, S0, strikes, float(t), sig, r, q,
                                      american=True)
            eu = binomial_price_batch(flags, S0, strikes, float(t), sig, r, q,
                                      american=False)
            eep[i] = am - eu
            # Zero time value where exercise-now is optimal: the price is pinned
            # at a POSITIVE intrinsic (far-OTM prices also vanish; exclude them).
            pinned[i] = (am - intrinsic < 1e-7 * np.maximum(strikes, S0)) & (
                intrinsic > 0.0)
        return eep, pinned

    eep_p, pin_p = panel(False, 0.04, 0.0)
    eep_c, pin_c = panel(True, R0, Q0)

    # Boundary macros at t = 0.5y: first strike (% spot) inside the dead zone.
    row = int(np.argmin(np.abs(mats - 0.5)))
    kp = 100.0 * strikes[pin_p[row]].min() / S0 if pin_p[row].any() else float("nan")
    kc = 100.0 * strikes[pin_c[row]].max() / S0 if pin_c[row].any() else float("nan")
    macro("deamstmapputbdry", "%.0f", kp)
    macro("deamstmapcallbdry", "%.0f", kc)
    macro("deamstmapputeep", "%.1f", eep_p[row].max())

    fig, axes = plt.subplots(1, 2, figsize=(7.6, 3.5), sharey=True)
    vmax = max(eep_p.max(), eep_c.max())
    for ax, eep, pin, title_lab, letter in (
        (axes[0], eep_p, pin_p, "put", "A"),
        (axes[1], eep_c, pin_c, "call", "B"),
    ):
        pc = ax.pcolormesh(KK, TT, eep, cmap="BuGn", vmin=0.0, vmax=vmax,
                           shading="auto", rasterized=True)
        ax.contour(KK, TT, pin.astype(float), levels=[0.5], colors=[RUST],
                   linewidths=1.6)
        ax.set_xlabel("strike (% of spot)")
        label_panel(ax, letter)
        ax.grid(False)
    axes[0].set_ylabel("time to expiry $t$ (years)")
    axes[0].annotate("price $=$ intrinsic:\nexercise now,\nno root exists",
                     xy=(138, 0.75), xytext=(72, 0.95), color=PALETTE["ink"],
                     fontsize=9, ha="left",
                     arrowprops={"arrowstyle": "->", "color": SLATE, "lw": 1.1})
    axes[1].annotate("dividends pull the\nboundary onto\ndeep ITM calls",
                     xy=(66, 0.75), xytext=(94, 0.95), color=PALETTE["ink"],
                     fontsize=9, ha="left",
                     arrowprops={"arrowstyle": "->", "color": SLATE, "lw": 1.1})
    cb = fig.colorbar(pc, ax=axes, pad=0.015, fraction=0.035)
    cb.set_label(r"early-exercise premium $\pi$ (dollars)", fontsize=10)
    save(fig, OUT / "fig_deamst_map.pdf")


