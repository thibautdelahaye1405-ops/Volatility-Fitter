def macro(name: str, fmt: str, value) -> None:
    MACROS.append((r"\newcommand{\%s}{" + fmt + "}") % (name, value))
    NUMBERS[name] = float(value) if isinstance(value, (int, float, np.floating)) else value


def naive_iv_calls(strikes: np.ndarray, am: np.ndarray, f: float, d: float,
                   t: float) -> np.ndarray:
    """American CALL dollar prices through the pipeline's analytic European
    inversion (normalize, invert to total variance, annualize)."""
    k = np.log(strikes / f)
    w = implied_total_variance(k, am / (d * f))
    with np.errstate(invalid="ignore"):
        return np.sqrt(w / t)


