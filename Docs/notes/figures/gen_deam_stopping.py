def figure_real(snap, s, f, d, t) -> None:
    puts = two_sided(snap, EXPIRY, side="P")
    ks = np.array([p.strike for p in puts])
    mids = np.array([p.mid for p in puts])
    keep = (ks >= 0.55 * s) & (ks <= 1.38 * s)
    ks, mids = ks[keep], mids[keep]
    is_call = np.zeros(ks.size, dtype=bool)
    deam = deamericanize_batch(is_call, mids, s, ks, t, R, Q)
    naive = naive_black_iv(is_call, mids, ks, f, d, t)
    ok = np.isfinite(deam) & np.isfinite(naive)
    ks, deam, naive = ks[ok], deam[ok], naive[ok]
    pct = 100.0 * ks / s
    bias_bp = 1e4 * (naive - deam)
    itm = pct > 100.0
    macro("deamstrealmedianitm", "%.0f", np.median(bias_bp[itm]))
    macro("deamstrealmaxbias", "%.0f", bias_bp.max())
    macro("deamstrealnputs", "%d", ks.size)
    macro("deamstrealnitm", "%d", int(itm.sum()))

    fig, ax = plt.subplots(figsize=(6.9, 4.2))
    ax.fill_between(pct, 100 * deam, 100 * naive, where=pct >= 100.0,
                    color=RUST, alpha=0.10, lw=0)
    ax.plot(pct, 100 * naive, color=RUST, lw=1.8,
            label="American mid inverted as European — biased")
    ax.plot(pct, 100 * deam, color=TEAL, ls="--", lw=2.0,
            label=r"de-Americanized $\sigma^*$ — European-equivalent")
    ax.axvline(100.0, color=SLATE, ls=":", lw=1.1, alpha=0.8)
    ax.annotate("ATM", xy=(100.0, 0.02), xycoords=("data", "axes fraction"),
                xytext=(3, 0), textcoords="offset points", color=SLATE, fontsize=9)
    i = int(np.argmin(np.abs(pct - 124.0)))
    ax.annotate(
        "the wedge = model-implied IV effect of $\\pi$\n"
        "on ITM puts production DISCARDS\n(median +%.0f bp, max +%.0f bp)"
        % (float(np.median(bias_bp[itm])), float(bias_bp.max())),
        xy=(pct[i], 50 * (naive[i] + deam[i])), xytext=(-192, 40),
        textcoords="offset points", color=PALETTE["ink"], fontsize=9,
        arrowprops={"arrowstyle": "-", "color": SLATE, "lw": 0.9},
    )
    ax.set_xlabel("put strike (% of spot)")
    ax.set_ylabel("implied volatility (%)")
    lo_y, hi_y = ax.get_ylim()
    ax.set_ylim(lo_y, hi_y + 0.05 * (hi_y - lo_y))
    ax.legend(frameon=False, loc="upper left", fontsize=9)
    fig.tight_layout()
    save(fig, OUT / "fig_deamst_real.pdf")


def figure_real_selected(snap, s, f, d, t) -> None:
    tick_floor = 3.0 * snap.tick_size if getattr(snap, "tick_size", None) else 0.0
    qs = [x for x in two_sided(snap, EXPIRY)
          if x.call_put == ("C" if x.strike >= f else "P") and x.mid > tick_floor]
    ks = np.array([x.strike for x in qs])
    mids = np.array([x.mid for x in qs])
    is_call = np.array([x.call_put == "C" for x in qs])
    # The liquid no-op regime of the convex gate: this fixture's normalized
    # spreads are tick-wide, an order below the 1e-3 repair tolerance.
    spread_norm = float(np.median(
        [(x.ask - x.bid) for x in qs])) / (d * f)
    ex = int(np.floor(np.log10(spread_norm)))
    macro("deamstliqmant", "%.1f", spread_norm / 10.0**ex)
    macro("deamstliqexp", "%d", ex)
    deam = deamericanize_batch(is_call, mids, s, ks, t, R, Q)
    naive = naive_black_iv(is_call, mids, ks, f, d, t)
    ok = np.isfinite(deam) & np.isfinite(naive)
    ks, is_call, deam, naive = ks[ok], is_call[ok], deam[ok], naive[ok]
    k_log = np.log(ks / f)
    w_atm = float(np.interp(0.0, k_log, deam**2 * t))
    wing = np.abs(k_log) <= 4.0 * np.sqrt(w_atm)
    ks, is_call, deam, naive = ks[wing], is_call[wing], deam[wing], naive[wing]
    pct = 100.0 * ks / s
    bias_bp = 1e4 * (naive - deam)
    puts, calls = bias_bp[~is_call], bias_bp[is_call]
    # The depth caution, measured on the fitted population: batch trees at 128
    # steps vs the shipped 192 (same bracketing, same bisections).
    d128 = deamericanize_batch(is_call, mids[ok][wing], s, ks, t, R, Q, n_steps=128)
    d192 = deamericanize_batch(is_call, mids[ok][wing], s, ks, t, R, Q, n_steps=192)
    both = np.isfinite(d128) & np.isfinite(d192)
    shift = 1e4 * np.abs(d128 - d192)[both]
    macro("deamstdepthrealmed", "%.0f", np.median(shift))
    macro("deamstdepthrealmax", "%.0f", shift.max())
    macro("deamstseln", "%d", ks.size)
    macro("deamstselabsmedian", "%.1f", np.median(np.abs(bias_bp)))
    macro("deamstselmax", "%.0f", bias_bp.max())
    macro("deamstselputmedian", "%.0f", np.median(puts))
    macro("deamstselputmax", "%.0f", puts.max())
    macro("deamstselcallmedian", "%.1f", np.median(calls))
    macro("deamstselcallmax", "%.1f", calls.max())

    fig, ax = plt.subplots(figsize=(6.9, 3.9))
    ax.plot(pct[~is_call], puts, "o", ms=4.5, color=TEAL,
            label="selected OTM puts (below $F$)")
    ax.plot(pct[is_call], calls, "s", ms=4.0, color=BLUE,
            label="selected OTM calls (above $F$)")
    ax.axvline(100.0 * f / s, color=SLATE, ls=":", lw=1.1, alpha=0.8)
    ax.annotate("$F$", xy=(100.0 * f / s, 0.9), xycoords=("data", "axes fraction"),
                xytext=(3, 0), textcoords="offset points", color=SLATE, fontsize=9)
    ax.annotate(
        "the fitted population: median $|$effect$|$ %.1f bp, max %.0f bp"
        % (float(np.median(np.abs(bias_bp))), float(bias_bp.max())),
        xy=(0.03, 0.86), xycoords="axes fraction", color=PALETTE["ink"], fontsize=9)
    ax.set_xlabel("strike (% of spot)")
    ax.set_ylabel("naive $-$ de-Am implied vol (vol bp)")
    ax.legend(frameon=False, loc="center left", fontsize=9)
    fig.tight_layout()
    save(fig, OUT / "fig_deamst_realsel.pdf")


