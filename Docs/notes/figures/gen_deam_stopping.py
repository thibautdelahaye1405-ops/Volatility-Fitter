    ax.loglog(depths, np.maximum(root_bp, 1e-3), "s", ms=4.2, color=TEAL,
              label=r"root $\sigma^*$ (level error only)")
    for n, tag in ((192, "batch"), (501, "scalar")):
        i = int(np.flatnonzero(depths == n)[0])
        ax.plot([n], [max(iv_bp[i], 1e-3)], "o", ms=9, mfc="none", mec=SLATE)
        ax.annotate(tag, xy=(n, max(iv_bp[i], 1e-3)), xytext=(0, 10),
                    ha="center", textcoords="offset points", fontsize=8.5,
                    color=SLATE)
    ax.set_xlabel("tree depth $N$")
    ax.set_ylabel("error at $K=90$ (vol bp)")
    ax.legend(loc="lower left", fontsize=8.5, handletextpad=0.4)
    ax.set_ylim(6e-4, 60)
    label_panel(ax, "A")
    ax = axes[1]
