def listing_min_butterfly(K: np.ndarray, C: np.ndarray) -> float:
    """The Appendix D listing, verbatim (verified against production below)."""
    a, b = K[1:-1] - K[:-2], K[2:] - K[1:-1]
    fly = C[:-2] * b - C[1:-1] * (a + b) + C[2:] * a
    return (fly / (a + b)).min()


def listing_convex_fit(u: np.ndarray, c: np.ndarray, slope_floor: float) -> np.ndarray:
    """The Appendix D listing, verbatim (verified against production below)."""
    from scipy.optimize import lsq_linear
    cols = [u] + [np.maximum(u - u[j], 0.0) for j in range(1, len(u) - 1)]
    A = np.column_stack(cols)
    lb = np.zeros(A.shape[1]); lb[0] = slope_floor
    x = lsq_linear(A, c - c[0], bounds=(lb, np.inf)).x
    return c[0] + A @ x


def figure_convex() -> None:
    """The wing-only, band-constrained repair on a controlled illiquid wing.

    The real fixture cannot stage this demo: its synthesized delayed-tier
    quotes carry tick-wide spreads (~3e-5 normalized), an order below the
    CONVEX_TOL gate — the liquid no-op regime.  So the mechanism demo is a
    constructed smile with realistic ILLIQUID wing spreads (the -1e-3..-3e-2
    regime the production gate was calibrated on) and a one-sided clamp-scale
    perturbation, repaired by the PRODUCTION projection."""
    from volfit.core.black import black_call

    t = 0.5
    # A smooth skewed smile in normalized units (F = 1); local formula only
    # defines the synthetic truth — pricing and repair are production calls.
    core_k = np.arange(-0.15, 0.121, 0.03)
    wing_k = np.array([-0.55, -0.47, -0.40, -0.34, -0.29, -0.25, -0.215, -0.185])
    k = np.concatenate([wing_k, core_k])
    iv = 0.22 - 0.28 * k + 0.10 * k * k
    c_true = black_call(k, iv * iv * t)
    w_atm = float(0.22**2 * t)
    z = k / np.sqrt(w_atm)
    wing = z < -1.0

    # Illiquid-regime band: tight core, wide sparse wing; then the max(EEP,0)
    # clamp footprint — one-sided UP bumps on alternating wing strikes (an
    # under-estimated premium leaves the shifted price too high).
    half = np.where(wing, 2.2e-3, 2.5e-4)
    c_lo, c_hi = c_true - half, c_true + half
    bump = np.where(wing & (np.arange(k.size) % 2 == 0), 0.85 * half, 0.0)
    c_pert = np.clip(c_true + bump, c_lo, c_hi)

    K = np.exp(k)

    def fly_curve(c: np.ndarray) -> np.ndarray:
        a_, b_ = K[1:-1] - K[:-2], K[2:] - K[1:-1]
        return (c[:-2] * b_ - c[1:-1] * (a_ + b_) + c[2:] * a_) / (a_ + b_)

    before = min_norm_butterfly(K, c_pert)
    repaired = convex_wing_repair(k, c_pert, c_lo, c_hi, w_atm, 1.0)
    if repaired is None:
        raise RuntimeError("repair did not engage (min butterfly %.2e)" % before)
    after = min_norm_butterfly(K, repaired)
    core = ~wing
    core_move = float(np.max(np.abs((repaired - c_pert)[core])))
    in_band = bool(np.all(repaired >= c_lo - 1e-12) and np.all(repaired <= c_hi + 1e-12))

    w_pert = implied_total_variance(k, c_pert)
    w_rep = implied_total_variance(k, repaired)
    iv_pert, iv_rep = np.sqrt(w_pert / t), np.sqrt(w_rep / t)
    iv_lo = np.sqrt(implied_total_variance(k, c_lo) / t)
    iv_hi = np.sqrt(implied_total_variance(k, c_hi) / t)
    macro("deamstcvxbefore", "%.1f", 1e3 * before)
    ex = int(np.floor(np.log10(abs(after)))) if after != 0.0 else 0
    macro("deamstcvxaftermant", "%.1f", after / 10.0**ex)
    macro("deamstcvxafterexp", "%d", ex)
    macro("deamstcvxmove", "%.0f", 1e4 * float(np.nanmax(np.abs(iv_rep - iv_pert))))
    macro("deamstcvxcore", "%.0f", core_move)
    macro("deamstcvxinband", "%s", "yes" if in_band else "NO")

    # Appendix D verification: the two listings against production, same inputs.
    d_fly = abs(listing_min_butterfly(K, c_pert) - before)
    anchor = int(np.flatnonzero(core)[0])
    u = (K[anchor] - K[: anchor + 1])[::-1]
    cw = c_pert[: anchor + 1][::-1]
    sl = convex_deam._core_slope(K, c_pert, anchor, +1)
    d_fit = float(np.max(np.abs(listing_convex_fit(u, cw, -sl)
                                - convex_deam._repair_wing(u, cw, -sl))))
    macro("deamstrefagree", "%.0f", np.floor(np.log10(max(d_fly, d_fit, 1e-16))))

    pctm = 100.0 * K
    show = pctm < 94.0
    fig, axes = plt.subplots(1, 2, figsize=WIDE)
    ax = axes[0]
    ax.fill_between(pctm[show], 100 * iv_lo[show], 100 * iv_hi[show], color=SLATE,
                    alpha=0.16, lw=0, label="bid–ask band")
    ax.plot(pctm[show], 100 * iv_pert[show], "o-", ms=3.8, lw=1.2, color=RUST,
            label="de-Am mid, clamp footprint")
    ax.plot(pctm[show], 100 * iv_rep[show], "-", lw=2.0, color=TEAL,
            label="repaired (wing-only projection)")
    zb = 100.0 * float(np.exp(-np.sqrt(w_atm)))
    ax.axvline(zb, color=SLATE, ls=":", lw=1.1)
    ax.annotate("core boundary\n$z=-1$", xy=(zb, 0.42),
                xycoords=("data", "axes fraction"), xytext=(5, 0),
                textcoords="offset points", fontsize=8.5, color=SLATE)
    ax.set_xlabel("strike (% of forward)")
    ax.set_ylabel("implied volatility (%)")
    ax.legend(loc="upper right", fontsize=8.5)
    label_panel(ax, "A")
    ax = axes[1]
    ax.axhspan(-1.0, 0.0, color=RUST, alpha=0.07, lw=0)
    ax.axhline(0.0, color=PALETTE["ink"], lw=0.8)
    ax.axhline(-1.0, color=RUST, lw=0.9, ls="--", alpha=0.7)
    ax.annotate("repair gate $10^{-3}$", xy=(0.02, 0.13), xycoords="axes fraction",
                fontsize=8.5, color=RUST)
    ax.plot(pctm[1:-1], 1e3 * fly_curve(c_pert), "o", ms=4.2, color=RUST,
            label="before")
    ax.plot(pctm[1:-1], 1e3 * fly_curve(repaired), "s", ms=3.8, color=TEAL,
            label="after")
    ax.set_xlabel("strike (% of forward)")
    ax.set_ylabel(r"normalized butterfly ($\times 10^{-3}$)")
    ax.legend(loc="lower right", fontsize=9)
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.3)
    save(fig, OUT / "fig_deamst_convex.pdf")


