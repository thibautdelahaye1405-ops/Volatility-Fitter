"""Figure suite for the units/measure/tolerance edition of Note 07 (objective).

Run from the repository root with the project virtual environment::

    .venv\\Scripts\\python.exe Docs\\notes\\figures\\gen_obj_measure.py

Every weight, band, residual, fit and RMS is produced by the PRODUCTION
objective machinery (``volfit.calib.{weights,band,rms}`` and the LQD slice
calibrator); local formulae define only the synthetic running smile and its
deterministic noise.  The running character is the fitted index-like smile
``RawSVI(a=0.0106, b=0.0729, rho=-0.5, m=0.0583, sigma=0.1010)`` shared with
the original Note-07 suite.

Outputs, written next to this script:

* ``fig_objm_units.pdf``   -- the change of units: price residuals vs vega-normalized;
* ``fig_objm_measure.pdf`` -- weights as quadrature: cumulative measure audit;
* ``fig_objm_tube.pdf``    -- the loss shapes: mid parabola vs banded tube + anchor;
* ``fig_objm_flex.pdf``    -- band value scales with model flexibility (real fits);
* ``fig_objm_dial.pdf``    -- the haircut dial sweeping mid -> band, honest RMS;
* ``fig_objm_anchor.pdf``  -- the tube's flat valley and the anchor's selection;
* ``obj_measure_tables.tex`` / ``obj_measure_numbers.json``.
"""

