def _cadence_moves() -> dict[str, float]:
    """Median |dz_ATM| per step type from the stored 0DTE measurement tables."""
    steps: dict[str, list[float]] = {"intraday": [], "overnight": [], "weekend": []}
    for tk in ("SPY", "QQQ", "IWM"):
        files = sorted(RESULTS.glob(f"intraday_filter/{tk}_*.json"))
        by_exp: dict[str, list[tuple[float, float]]] = {}
        for f in files:
            for row in json.loads(f.read_text()):
                ts = datetime.fromisoformat(row["ts"]).timestamp()
                by_exp.setdefault(row["expiry"], []).append(
                    (ts, float(row["z"][0])))
        for series in by_exp.values():
            series.sort()
            for (t0, z0), (t1, z1) in zip(series, series[1:]):
                gap_h = (t1 - t0) / 3600.0
                if gap_h <= 0:
                    continue
                if gap_h < 8:
                    steps["intraday"].append(abs(z1 - z0))
                elif gap_h < 40:
                    steps["overnight"].append(abs(z1 - z0))
                else:
                    steps["weekend"].append(abs(z1 - z0))
    return {k: 1e4 * float(np.median(v)) for k, v in steps.items() if v}


def figure_clock() -> None:
    moves = _cadence_moves()
    macro("ktrmoveintra", "%.0f", moves["intraday"])
    macro("ktrmoveovernight", "%.0f", moves["overnight"])
    macro("ktrmoveweekend", "%.0f", moves["weekend"])

    t0 = datetime(2026, 7, 1, 4, 0)  # Wed 00:00 ET as naive UTC
    grid = [t0 + timedelta(minutes=30 * i) for i in range(7 * 48 + 1)]
    cal = [(t - t0).total_seconds() / 86400.0 for t in grid]
    ses = [intraday_variance_days(t0, t, 0.60, 0.0) for t in grid]

    sweep = json.loads((RESULTS / "intraday_filter" / "sweep.json").read_text())
    cal_rows = {(r["process_bp"], r["step"]): r for r in sweep
                if r["clock"] == "calendar"}
    bps = sorted({bp for bp, _ in cal_rows})
    best_bp = min(bps, key=lambda bp: abs(cal_rows[(bp, "intraday")]["zeta_std"][0] - 1.0))
    ses_rows = {r["step"]: r for r in sweep
                if r["clock"] == "session60w0" and r["process_bp"] == 90.0}
    steps = ("intraday", "overnight", "weekend")
    z_cal = [cal_rows[(best_bp, s)]["zeta_std"][0] for s in steps]
    z_ses = [ses_rows[s]["zeta_std"][0] for s in steps]
    macro("ktrcalbestq", "%.0f", best_bp)
    for s, zc, zs in zip(("intra", "overnight", "weekend"), z_cal, z_ses):
        macro("ktrzcal" + s, "%.2f", zc)
        macro("ktrzses" + s, "%.2f", zs)

    fig, axes = plt.subplots(1, 2, figsize=WIDE,
                             gridspec_kw={"width_ratios": [1.3, 1.0]})
    ax = axes[0]
    ax.plot(cal, cal, color=SLATE, lw=1.7, ls="--", label="calendar clock")
    ax.plot(cal, ses, color=TEAL, lw=1.9,
            label="session clock (share 0.60, closed days 0)")
    ax.axvspan(1.0 + 16.0 / 24.0, 5.0 + 9.5 / 24.0, color="black", alpha=0.05)
    ax.annotate("Jul 3 holiday + weekend:\nthe market is closed,\nvariance stops accruing",
                xy=(3.6, 0.55), fontsize=8.8, color=SLATE, ha="center")
    ax.set_xlabel("days since Wed 00:00 ET (2026-07-01)")
    ax.set_ylabel("accrued variance-days")
    ax.legend(loc="upper left", fontsize=8.7)
    label_panel(ax, "A")
    ax = axes[1]
    x = np.arange(3)
    ax.bar(x - 0.19, z_cal, width=0.36, color=SLATE,
           label=f"calendar, best $q$ ({best_bp:.0f} bp)")
    ax.bar(x + 0.19, z_ses, width=0.36, color=TEAL,
           label="session, $q=90$ bp")
    ax.axhline(1.0, color=AMBER, lw=1.4, ls="--")
    ax.text(-0.38, 1.045, "calibrated", color=AMBER, fontsize=8.2)
    ax.set_xticks(x, ("30-min", "overnight", "weekend"))
    ax.set_ylabel(r"$\zeta$ std (ATM)")
    ax.set_ylim(0, 1.34)
    ax.legend(loc="upper right", fontsize=8.0)
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.34)
    save(fig, OUT / "fig_ktr_clock.pdf")


