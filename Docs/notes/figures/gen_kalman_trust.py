def _rows(stem: str, v2: bool = False) -> list[dict]:
    suffix = "_observation_filter_v2_merged.json" if v2 else "_observation_filter_merged.json"
    return json.loads((RESULTS / f"{stem}{suffix}").read_text())["summary"]


def _cell(rows, scenario, cov, bp, bucket, mode=None):
    for r in rows:
        if (r["scenario"] == scenario and r["cov_mode"] == cov
                and float(r["process_bp"]) == bp and r["bucket"] == bucket
                and (mode is None or r.get("mode") == mode)):
            return r
    raise KeyError((scenario, cov, bp, bucket, mode))


