def figure_offdiag() -> None:
    sd_p = np.array([0.0030, 0.08, 0.05])
    sd_r = np.array([0.0015, 0.05, 0.30])
    m = np.array([0.20, -0.35, 0.10])
    z = m + np.array([0.0, 0.0, 0.45])  # junk curvature innovation only
    cs = np.linspace(0.0, 0.95, 40)
    err_full, err_diag = [], []
    for c in cs:
        corr = np.eye(3)
        corr[0, 2] = corr[2, 0] = c
        R = corr * np.outer(sd_r, sd_r)
        upd_f = kalman_update(m, np.diag(sd_p**2), z, R)
        upd_d = kalman_update(m, np.diag(sd_p**2), z, np.diag(sd_r**2))
        err_full.append(1e4 * abs(upd_f.mean[0] - m[0]))
        err_diag.append(1e4 * abs(upd_d.mean[0] - m[0]))
    macro("ktroffdiagdrag", "%.0f", err_full[-1])

    fig, ax = plt.subplots(figsize=(6.9, 3.8))
    ax.plot(cs, err_full, color=RUST, lw=2.0,
            label="full-covariance update")
    ax.plot(cs, err_diag, color=TEAL, lw=2.0,
            label="diagonal update (production)")
    callout(ax, "a junk curvature innovation drags the\nATM level through "
                "$K_{13}$ — the level was\nnever observed to move",
            xy=(0.86, err_full[-5]), xytext=(0.05, 0.62 * max(err_full)))
    ax.set_xlabel("level–curvature correlation stated in $R$")
    ax.set_ylabel("ATM posterior error (vol bp)")
    ax.legend(loc="upper left")
    save(fig, OUT / "fig_ktr_offdiag.pdf")


