def figure_contra() -> None:
    i, j = 12, 13  # adjacent strikes just right of the money
    eps_grid = np.linspace(0.0, 0.03, 13)
    q_diag, _ = process_noise(1.0, 0.0, vol_bp_sqrt_day=30.0)
    m_pred = None
    rows = []
    for eps in eps_grid:
        sig = base_sigma(K21).copy()
        sig[i] += eps
        sig[j] -= eps
        meas, _, _ = fit_measurement(K21, sig * sig * TAU, NOISE)
        if m_pred is None:  # the clean fit is the transported prediction
            m_pred = meas.handles.copy()
        p_diag = q_diag + np.array([0.003, 0.08, 0.5]) ** 2
        upd = kalman_update(m_pred, np.diag(p_diag), meas.handles,
                            np.diag(np.diag(meas.cov)))
        rows.append((meas.breakdown["rho"], np.sqrt(np.diag(meas.cov)),
                     np.diag(upd.gain)))
    rho = np.array([r[0] for r in rows])
    r_std = np.array([r[1] for r in rows])
    gains = np.array([r[2] for r in rows])
    macro("ktrcontrarho", "%.0f", rho[-1])
    macro("ktrcontragainclean", "%.2f", gains[0, 2])
    macro("ktrcontragainkink", "%.2f", gains[-1, 2])
    macro("ktrcontragainatm", "%.2f", gains[-1, 0])

    fig, axes = plt.subplots(1, 2, figsize=WIDE)
    ax = axes[0]
    x = 100 * eps_grid
    ax.plot(x, r_std[:, 2], color=RUST, lw=1.9,
            label=r"$\sqrt{R_{\kappa\kappa}}$ (curvature)")
    ax.set_xlabel("contradiction size (vol points)")
    ax.set_ylabel("stated curvature noise", color=RUST)
    ax2 = ax.twinx()
    ax2.plot(x, rho, color=SLATE, lw=1.8, ls="--")
    ax2.set_ylabel(r"inflation $\rho=\chi^2/(m-d)$", color=SLATE)
    ax2.grid(False)
    ax2.spines["top"].set_visible(False)
    ax.legend(loc="upper left")
    label_panel(ax, "A")
    ax = axes[1]
    for h, c in ((0, TEAL), (1, AMBER), (2, RUST)):
        ax.plot(x, gains[:, h], color=c, lw=1.9, label=HANDLE_LABELS[h])
    callout(ax, "nobody turned this dial down:\nthe misfit did",
            xy=(2.4, gains[-3, 2] + 0.015), xytext=(1.02, 0.44))
    ax.set_xlabel("contradiction size (vol points)")
    ax.set_ylabel("computed gain $K$")
    ax.set_ylim(-0.03, 1.05)
    ax.legend(loc="lower left", fontsize=8.7)
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.5)
    save(fig, OUT / "fig_ktr_contra.pdf")


