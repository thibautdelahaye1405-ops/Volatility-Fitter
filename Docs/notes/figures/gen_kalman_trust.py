TAU = 0.5
K21 = np.linspace(-0.25, 0.25, 21)
NOISE = 0.001  # 10 bp stated per-quote noise


def base_sigma(k):
    k = np.asarray(k, dtype=float)
    return 0.20 - 0.35 * k + 0.6 * k * k


def fit_measurement(k, w, noise_scale, n_order=6, reg_lambda=1e-6):
    """Production fit -> production Jacobian-route measurement (z, R)."""
    sd: dict = {}
    fit = calibrate_slice(k, w, TAU, n_order=n_order, reg_lambda=reg_lambda,
                          solver_diag=sd)
    h = atm_handles(fit.slice, TAU)
    z = np.array([h.sigma0, h.skew, h.curvature])

    def handle_fn(theta):
        s = build_slice(LQDParams.from_vector(theta), n_points=OPT_N_POINTS)
        hh = atm_handles(s, TAU)
        return np.array([hh.sigma0, hh.skew, hh.curvature])

    g = handle_jacobian_fd(handle_fn, sd["theta"])
    meas = measurement_from_jacobian(
        z, sd["jac"], g, sd["residual"], sd["n_fit_rows"], sd["n_quotes"],
        noise_scale=noise_scale,
    )
    return meas, sd, g


