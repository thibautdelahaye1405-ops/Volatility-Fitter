def figure_units() -> None:
    w = base_sigma(K21) ** 2 * TAU
    noises = np.geomspace(5e-4, 1e-2, 9)
    atm_std = [float(np.sqrt(fit_measurement(K21, w, s)[0].cov[0, 0]))
               for s in noises]
    macro("ktrunitslope", "%.2f",
          float(np.polyfit(np.log(noises), np.log(atm_std), 1)[0]))

    counts = (21, 13, 9, 7, 5)
    curv_std, clamped = [], []
    for n in counts:
        kq = np.linspace(-0.25, 0.25, n)
        meas, sd, g = fit_measurement(kq, base_sigma(kq) ** 2 * TAU, NOISE)
        curv_std.append(float(np.sqrt(meas.cov[2, 2])))
        # re-derive the clamp count from the production algebra
        jac2 = sd["jac"].copy()
        jac2[: sd["n_fit_rows"]] /= NOISE
        _, n_cl = covariance_from_information(g, information_matrix(jac2))
        clamped.append(n_cl)
    macro("ktrclampsparse", "%d", clamped[-1])
    # the clamp as the LAST fence: same sparse chain with the intrinsic
    # regularization rows removed (reg 0) — the information is genuinely
    # rank-deficient and the eigen-clamp keeps R finite.
    kq = np.linspace(-0.25, 0.25, counts[-1])
    _, sd0, g0 = fit_measurement(kq, base_sigma(kq) ** 2 * TAU, NOISE,
                                 reg_lambda=0.0)
    jac0 = sd0["jac"].copy()
    jac0[: sd0["n_fit_rows"]] /= NOISE
    _, n_cl0 = covariance_from_information(g0, information_matrix(jac0))
    macro("ktrclampnoreg", "%d", n_cl0)

    fig, axes = plt.subplots(1, 2, figsize=WIDE)
    ax = axes[0]
    ax.loglog(1e4 * noises, atm_std, "o-", ms=5, color=TEAL, lw=1.8)
    ax.set_xlabel("stated per-quote noise $s_q$ (vol bp)")
    ax.set_ylabel(r"$\sqrt{R_{\sigma\sigma}}$ (ATM)")
    callout(ax, "slope 1: double the stated\nnoise, quadruple the covariance",
            xy=(1e4 * noises[5], atm_std[5]), xytext=(1e4 * noises[0], atm_std[-3]))
    label_panel(ax, "A")
    ax = axes[1]
    ax.plot(counts, curv_std, "o-", ms=5, color=RUST, lw=1.8)
    for n, cs, cl in zip(counts, curv_std, clamped):
        if cl > 0:
            ax.annotate(f"{cl} clamped", xy=(n, cs), xytext=(n + 0.2, cs * 0.82),
                        fontsize=8.5, color=SLATE)
    ax.set_xlabel("quotes on the chain")
    ax.set_ylabel(r"$\sqrt{R_{\kappa\kappa}}$ (curvature)")
    ax.set_yscale("log")
    ax.invert_xaxis()
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.42)
    save(fig, OUT / "fig_ktr_units.pdf")


