def target_for(pct: float, vol_quote: float) -> VarSwapTarget:
    # Production semantics (service.varswap_target): weight is a fraction of
    # the node's summed option weight (= quote count under equal weights).
    weight = (pct / 100.0) * K_QUOTES.size
    return VarSwapTarget(total_var=float(vol_quote**2 * T), weight=weight, t=T)


VOL_TARGET = VOL_CLOSED + 0.015


def figure_pull() -> None:
    pulled = calibrate_slice(K_QUOTES, W_QUOTES, T, n_order=6, reg_lambda=1e-6,
                             var_swap=target_for(10.0, VOL_TARGET))
    vol_pulled = float(np.sqrt(pulled.slice.var_swap_strike() / T))
    macro("vsrbasefitbp", "%.1f", 1e4 * float(np.max(np.abs(
        BASE.slice.implied_vol(K_QUOTES, T) - np.sqrt(W_QUOTES / T)))))
