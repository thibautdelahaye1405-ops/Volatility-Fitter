def figure_hero() -> None:
    fixture = REPO / "tmp" / "volfit_surfaces_20260718_1324.json"
    if not fixture.exists():
        raise FileNotFoundError(
            f"standing reference export not found: {fixture} — regenerate via "
            "the app's surfaces export (see memory: reference-live-fixture)")
    doc = json.loads(fixture.read_text(encoding="utf-8"))
    spy = next(tk for tk in doc["tickers"] if tk["ticker"] == "SPY")
    ts, published, rebuilt, atm = [], [], [], []
    for node in spy["nodes"]:
        p = node["lqdParams"]
        s = build_slice(LQDParams(L=p["L"], R=p["R"], a=np.array(p["a"])))
        ts.append(node["t"])
        published.append(node["varSwapVol"])
        rebuilt.append(float(np.sqrt(s.var_swap_strike() / node["t"])))
        ks = np.array([c["k"] for c in node["curve"]])
        ivs = np.array([c["iv"] for c in node["curve"]])
        atm.append(float(np.interp(0.0, ks, ivs)))
    ts, published = np.array(ts), np.array(published)
    rebuilt, atm = np.array(rebuilt), np.array(atm)
    macro("vsrheroagreebp", "%.2f", 1e4 * np.max(np.abs(rebuilt - published)))
    macro("vsrherospreadbp", "%.0f", 1e4 * np.max(rebuilt - atm))
    macro("vsrheronodes", "%d", ts.size)

    fig, ax = plt.subplots(figsize=(6.9, 3.8))
    ax.semilogx(ts, 100 * published, "o-", ms=5, color=TEAL, lw=1.8,
                label="published fair var-swap vol")
    ax.semilogx(ts, 100 * rebuilt, "x", ms=7, color=RUST, mew=1.6,
                label="closed form $-2\\,\\mathbb{E}[X]$, re-derived")
    ax.semilogx(ts, 100 * atm, "s--", ms=4, color=SLATE, lw=1.4,
                label="ATM implied vol")
    ax.set_xlabel(r"expiry $T$ (years, log scale)")
    ax.set_ylabel("volatility (%)")
    ax.legend(fontsize=8.5, loc="lower right")
    save(fig, OUT / "fig_vsr_hero.pdf")


