def listing_replication(implied_w, half=6.0, points=801):
    k = np.linspace(-half, half, points)
    w = np.maximum(implied_w(k), 1e-12)
    integ = black_call(k, w) * np.exp(-k)
    put = k < 0
    integ[put] += 1 - np.exp(-k[put])
    return 2.0 * np.trapezoid(integ, k)


def listing_var_swap_strike(z, q_z, u):
    return -2.0 * np.trapezoid(q_z * u * (1 - u), z)


def verify_listings() -> None:
    d1 = abs(listing_replication(BASE.slice.implied_w)
             - varswap_total_variance(BASE.slice.implied_w))
    s = BASE.slice
    d2 = abs(listing_var_swap_strike(s.z, s.q_z, s.u) - s.var_swap_strike())
    macro("vsrrefagree", "%.0f", np.floor(np.log10(max(d1, d2, 1e-17))))


