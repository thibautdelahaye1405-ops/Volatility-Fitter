XI = np.array([0.0, 0.70, 0.90, 1.00, 1.10, 1.30, 2.20])
TAU = np.array([0.0, 0.25, 0.5])
X_GRID = 0.01 * np.arange(221)
T_GRID = 0.005 * np.arange(101)


def _w_at(t: float, k: np.ndarray) -> np.ndarray:
    return RAW.total_variance(k) * (t / T)


def _t_to(expiry: float) -> np.ndarray:
    return 0.005 * np.arange(int(round(expiry / 0.005)) + 1)


def fit_lv_surface() -> AffineVarianceSurface:
    xs = np.array([0.75, 0.85, 0.92, 1.00, 1.08, 1.20, 1.35])
    quotes = []
    for t in (0.25, 0.5):
        k = np.log(xs)
        prices = black_call(k, _w_at(t, k))
        quotes += [OptionQuote(t=t, x=float(x), price=float(p), tol=2e-4)
                   for x, p in zip(xs, prices)]
    flat = AffineVarianceSurface(t_nodes=TAU, x_nodes=XI,
                                 theta=np.full((TAU.size, XI.size), 0.06))
    cal = calibrate_affine(flat, quotes, X_GRID, T_GRID,
                           reg_lambda=5.0, bounds=(0.005, 0.5))
    return cal.surface


LV = fit_lv_surface()
I_SOURCE, _ = solve_varswap_source(LV, X_GRID, _t_to(T))
Q_W, Q_C = varswap_weights(X_GRID, 0.01), varswap_const(X_GRID, 0.01)


def lv_static(expiry: float, sol=None):
    if sol is None:
        sol = solve_affine_dupire(LV, X_GRID, T_GRID, [expiry])
        return float(Q_W @ sol.prices[0] + Q_C)
    return sol


