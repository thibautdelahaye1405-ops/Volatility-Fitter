def figure_three() -> None:
    macro("vsrclosed", "%.2f", 100 * VOL_CLOSED)
    macro("vsrrepl", "%.2f", 100 * VOL_REPL)
    macro("vsragreebp", "%.2f", 1e4 * abs(VOL_CLOSED - VOL_REPL))
    vol_lv = float(np.sqrt(I_SOURCE / T))
    macro("vsrlvvol", "%.2f", 100 * vol_lv)
    macro("vsrlvgapbp", "%.0f", 1e4 * abs(vol_lv - VOL_CLOSED))

    fig, axes = plt.subplots(1, 3, figsize=(7.9, 2.9))

    ax = axes[0]  # strike side
    kk = np.linspace(-1.25, 1.25, 401)
    integ = strike_integrand(BASE.slice.implied_w, kk)
    ax.fill_between(kk, integ, color=TEAL, alpha=0.20, lw=0)
    ax.plot(kk, integ, color=TEAL, lw=1.8)
    ax.plot(K_QUOTES, strike_integrand(BASE.slice.implied_w, K_QUOTES), "o",
            ms=2.8, color=RUST)
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel("integrand")
    label_panel(ax, "A")

    ax = axes[1]  # quantile side
    s = BASE.slice
    m = np.abs(s.z) <= 12.0
    integ_z = -2.0 * s.q_z[m] * s.u[m] * (1.0 - s.u[m])
    ax.fill_between(s.z[m], integ_z, 0.0, where=integ_z >= 0, color=TEAL,
                    alpha=0.20, lw=0)
    ax.fill_between(s.z[m], integ_z, 0.0, where=integ_z < 0, color=RUST,
                    alpha=0.20, lw=0)
    ax.plot(s.z[m], integ_z, color=TEAL, lw=1.8)
    ax.axhline(0.0, color=PALETTE["ink"], lw=0.7, ls=":")
    ax.set_xlabel(r"logit coordinate $z$")
    label_panel(ax, "B")

    ax = axes[2]  # generator side
    tt = np.linspace(0.0, T, 60)
    xx = np.linspace(0.72, 1.40, 90)
    nu = np.array([LV.variance(xx, float(t)) for t in tt])
    pc = ax.pcolormesh(xx, tt, nu, cmap="viridis", shading="auto",
                       rasterized=True)
    # Occupation-measure contours: rho = d^2 C / dx^2 from production prices.
    exps = [round(0.02 * i, 2) for i in range(1, 26)]
    sol = solve_affine_dupire(LV, X_GRID, T_GRID, exps)
    dens = np.array([np.gradient(np.gradient(p, 0.01), 0.01)
                     for p in sol.prices])
    mx = (X_GRID >= xx[0]) & (X_GRID <= xx[-1])
    ax.contour(X_GRID[mx], exps, dens[:, mx], levels=[1.0, 3.0],
               colors="white", linewidths=0.9, alpha=0.9)
    ax.plot([1.0], [0.0], marker="v", ms=6, color="white", clip_on=False)
    ax.set_xlabel(r"spot ratio $x$")
    ax.set_ylabel(r"time $t$")
    cb = fig.colorbar(pc, ax=ax, pad=0.02, fraction=0.055)
    cb.ax.tick_params(labelsize=8)
    label_panel(ax, "C")

    fig.subplots_adjust(wspace=0.32)
    save(fig, OUT / "fig_vsr_three.pdf")


