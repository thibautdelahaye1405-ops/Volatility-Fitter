def figure_mass() -> None:
    k = np.linspace(-VS_HALF_WIDTH, VS_HALF_WIDTH, VS_POINTS)
    integ = strike_integrand(BASE.slice.implied_w, k)
    cum = np.concatenate([[0.0], np.cumsum(0.5 * (integ[1:] + integ[:-1])
                                           * np.diff(k))])
    cum /= cum[-1]
    span = (k >= K_QUOTES[0]) & (k <= K_QUOTES[-1])
    inside = float(np.trapezoid(integ[span], k[span])
                   / np.trapezoid(integ, k))
    macro("vsrwingsharepct", "%.0f", 100 * (1.0 - inside))
    atm = np.abs(k) <= 0.10
    macro("vsratmsharepct", "%.0f",
          100 * float(np.trapezoid(integ[atm], k[atm]) / np.trapezoid(integ, k)))

    # Error budget vs the EXACT closed form: truncation alone (fixed fine
    # spacing dk = 0.001, whose own discretization floor is ~0.007 bp) vs the
    # production rule (801 points, spacing grows with H).  Same production
    # integrator, different grids.
    hws = np.linspace(1.0, 8.0, 29)

    def repl_vol(h: float, pts: int) -> float:
        return float(np.sqrt(varswap_total_variance(
            BASE.slice.implied_w, half_width=h, points=pts) / T))

    fine = np.array([abs(repl_vol(float(h), 2 * int(h / 0.001) + 1)
                         - VOL_CLOSED) for h in hws]) * 1e4
    prod = np.array([abs(repl_vol(float(h), VS_POINTS) - VOL_CLOSED)
                     for h in hws]) * 1e4
    i6 = int(np.argmin(np.abs(hws - 6.0)))
    macro("vsrtruncsixbp", "%.3f", fine[i6])
    macro("vsrtruncplateau", "%.1f", hws[np.argmax(fine < 0.02)])
    macro("vsrprodeightbp", "%.2f", prod[-1])
    macro("vsrreplfinebp", "%.2f",
          1e4 * abs(repl_vol(6.0, 4001) - VOL_CLOSED))

    fig, axes = plt.subplots(1, 2, figsize=WIDE)
    ax = axes[0]
    disp = np.abs(k) <= 1.4
    ax.axvspan(K_QUOTES[0], K_QUOTES[-1], color=AMBER, alpha=0.14, lw=0,
               label="quoted strikes")
    ax.plot(k[disp], cum[disp], color=TEAL, lw=2.0,
            label="cumulative share of $w_{\\mathrm{vs}}$")
    ax.axhline(1.0, color=PALETTE["ink"], lw=0.7, ls=":")
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel("cumulative share")
    ax.legend(fontsize=8.5, loc="upper left")
    label_panel(ax, "A")
    ax = axes[1]
    ax.semilogy(hws, np.maximum(fine, 1e-5), color=TEAL, lw=1.8,
                label=r"fixed spacing $\Delta k=0.001$ (truncation)")
    ax.semilogy(hws, np.maximum(prod, 1e-5), color=RUST, lw=1.8, ls="--",
                label=r"fixed 801 points ($\Delta k$ grows)")
    ax.plot([6.0], [prod[i6]], "o", ms=6, color=RUST)
    callout(ax, "production grid", (6.0, prod[i6]), (4.1, 6.0))
    ax.set_ylim(1e-5, 30)
    ax.set_xlabel(r"replication half-width $H$")
    ax.set_ylabel("error vs closed form (vol bp)")
    ax.legend(fontsize=8, loc="lower right")
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.30)
    save(fig, OUT / "fig_vsr_mass.pdf")


