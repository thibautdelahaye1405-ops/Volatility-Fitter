# Figure 4: who pays — sequential bulldoze vs the symmetric split.
# ---------------------------------------------------------------------------


def figure_solver() -> None:
    t_n, t_f = 0.25, 0.50
    w_n = smile_w(K_PAIR, 0.30, 0.60, t_n)
    w_f = smile_w(K_PAIR, 0.20, 0.25, t_f)
    quotes = [ExpiryQuotes(t=t_n, k=K_PAIR, w=w_n),
              ExpiryQuotes(t=t_f, k=K_PAIR, w=w_f)]

    free_n = calibrate_slice(K_PAIR, w_n, t_n)
    free_f = calibrate_slice(K_PAIR, w_f, t_f)
    macro("calmviolfree", "%.4f",
          calendar_violation(free_n.slice, free_f.slice))

    seq = calibrate_surface(quotes, enforce_calendar=True)
    sym_fit, repair = calibrate_surface_symmetric(quotes)
    sym_slices = [build_slice(LQDParams.from_vector(th))
                  for th in repair.thetas]
    window = common_support(K_PAIR, K_PAIR)
    sci_macro("calmviolcured", max(0.0, calendar_violation_windowed(
        sym_slices[0], sym_slices[1], window)))

    def err_bp(slice_, w, t):
        return 1e4 * float(np.max(np.abs(slice_.implied_vol(K_PAIR, t)
                                         - np.sqrt(w / t))))

    seq_e = [err_bp(seq.results[0].slice, w_n, t_n),
             err_bp(seq.results[1].slice, w_f, t_f)]
    sym_e = [err_bp(sym_slices[0], w_n, t_n),
             err_bp(sym_slices[1], w_f, t_f)]
    macro("calmseqnearbp", "%.1f", seq_e[0])
    macro("calmseqfarbp", "%.0f", seq_e[1])
    macro("calmsymnearbp", "%.0f", sym_e[0])
    macro("calmsymfarbp", "%.0f", sym_e[1])

    fig, axes = plt.subplots(1, 2, figsize=WIDE,
                             gridspec_kw={"width_ratios": [1.0, 1.25]})
    ax = axes[0]
    x = np.arange(2)
    ax.bar(x - 0.18, seq_e, width=0.34, color=RUST, label="sequential")
    ax.bar(x + 0.18, sym_e, width=0.34, color=TEAL, label="symmetric")
    ax.set_xticks(x, ["near expiry", "far expiry"])
    ax.set_ylabel("worst quote error (vol bp)")
    ax.legend(fontsize=8.5, loc="upper left")
    label_panel(ax, "A")
    ax = axes[1]
    kk = np.linspace(-0.40, 0.34, 300)
    ax.plot(kk, free_n.slice.implied_w(kk), color=SLATE, lw=1.2, ls=":",
            label="free fits (crossing)")
    ax.plot(kk, free_f.slice.implied_w(kk), color=SLATE, lw=1.2, ls=":")
    wn_free, wf_free = free_n.slice.implied_w(kk), free_f.slice.implied_w(kk)
    ax.fill_between(kk, wn_free, wf_free, where=wf_free < wn_free,
                    color=RUST, alpha=0.15, lw=0)
    ax.plot(kk, sym_slices[1].implied_w(kk), color=TEAL, lw=2.4,
            label="far, symmetric repair")
    ax.plot(kk, sym_slices[0].implied_w(kk), color=RUST, lw=1.6, ls="--",
            label="near, symmetric repair (coincides)")
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel(r"total variance $w(k)$")
    ax.legend(fontsize=8, loc="upper right")
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.28)
    save(fig, OUT / "fig_calm_solver.pdf")


