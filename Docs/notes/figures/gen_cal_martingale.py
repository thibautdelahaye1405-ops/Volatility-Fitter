def figure_hero() -> None:
    fixture = REPO / "tmp" / "volfit_surfaces_20260718_1324.json"
    if not fixture.exists():
        raise FileNotFoundError(
            f"standing reference export not found: {fixture} — regenerate via "
            "the app's surfaces export (see memory: reference-live-fixture)")
    doc = json.loads(fixture.read_text(encoding="utf-8"))
    spy = next(tk for tk in doc["tickers"] if tk["ticker"] == "SPY")
    nodes = []
    for n in spy["nodes"]:
        ks = np.array([c["k"] for c in n["curve"]])
        ws = np.array([c["w"] for c in n["curve"]])
        cols = n["inputs"]["preparedColumns"]
        prep = np.array(n["inputs"]["prepared"], dtype=float)
        kq = prep[:, cols.index("k")]
        nodes.append((n["expiry"], ks, ws, kq.min(), kq.max()))
    macro("calmheronodes", "%d", len(nodes))

    # Adjacent-pair order: margin on the common traded span, crossing beyond.
    margin, phantom = np.inf, 0.0
    for (e1, k1, w1, qlo1, qhi1), (e2, k2, w2, qlo2, qhi2) in zip(nodes, nodes[1:]):
        lo, hi = max(k1.min(), k2.min()), min(k1.max(), k2.max())
        kk = np.linspace(lo, hi, 600)
        diff = np.interp(kk, k1, w1) - np.interp(kk, k2, w2)  # near - far
        tlo, thi = max(qlo1, qlo2), min(qhi1, qhi2)
        m = (kk >= tlo) & (kk <= thi)
        margin = min(margin, -float(diff[m].max()))
        phantom = max(phantom, float(diff.max()))
    macro("calmheromarginbp", "%.1f", 1e4 * margin)
    macro("calmherophantombp", "%.0f", 1e4 * phantom)

    fig, ax = plt.subplots(figsize=(6.9, 4.1))
    cmap = plt.get_cmap("viridis")
    for i, (exp, ks, ws, qlo, qhi) in enumerate(nodes):
        col = cmap(0.12 + 0.75 * i / (len(nodes) - 1))
        ax.semilogy(ks, ws, color=col, lw=1.0, alpha=0.45)
        m = (ks >= qlo) & (ks <= qhi)
        ax.semilogy(ks[m], ws[m], color=col, lw=2.6, label=exp)
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel(r"total variance $w(k)$ (log scale)")
    ax.legend(fontsize=7.5, loc="lower left", ncol=2, title="expiry",
              title_fontsize=8)
    callout(ax, "quoted spans: strict convex order\n(thick segments never touch)",
            xy=(0.05, float(np.interp(0.05, nodes[2][1], nodes[2][2]))),
            xytext=(0.25, 3e-3))
    callout(ax, "drawn extrapolations\ndisagree out here",
            xy=(-1.1, float(np.interp(-1.1, nodes[0][1], nodes[0][2]))),
            xytext=(-1.32, 2.5e-1))
    save(fig, OUT / "fig_calm_hero.pdf")


