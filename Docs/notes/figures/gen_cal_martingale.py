# ---------------------------------------------------------------------------


def figure_phantom() -> None:
    near_steep = RawSVI(a=0.01, b=0.20, rho=-0.7, m=0.0, sigma=0.05)
    far_flat = RawSVI(a=0.08, b=0.08, rho=-0.3, m=0.0, sigma=0.10)
    k_narrow = np.linspace(-0.25, 0.20, 21)
    w_far = far_flat.total_variance(k_narrow)
    t = 1.0

    wide_k, wide_w = variance_floor_targets(near_steep, variance_floor_grid())
    wide = calibrate_svi(k_narrow, w_far, t=t, calendar_k=wide_k,
                         calendar_floor=wide_w)
    data_k, data_w = variance_floor_targets(
        near_steep, variance_floor_grid_from(k_narrow))
    data = calibrate_svi(k_narrow, w_far, t=t, calendar_k=data_k,
                         calendar_floor=data_w)
    macro("calmphantomwidebp", "%.0f", wide.max_iv_error * 1e4)
    macro("calmphantomdatabp", "%.1f", data.max_iv_error * 1e4)

    fig, axes = plt.subplots(1, 2, figsize=WIDE,
                             gridspec_kw={"width_ratios": [1.2, 1.0]})
    ax = axes[0]
    kk = np.linspace(-1.05, 1.05, 400)
    wn, wf = near_steep.total_variance(kk), far_flat.total_variance(kk)
    ax.axvspan(k_narrow.min(), k_narrow.max(), color=AMBER, alpha=0.16, lw=0)
    ax.plot(kk, wn, color=RUST, lw=1.8, label="near (steep wings)")
    ax.plot(kk, wf, color=TEAL, lw=1.8, label="far (flat, true)")
    ax.fill_between(kk, wf, wn, where=wn > wf, color=RUST, alpha=0.13, lw=0)
    ax.plot(wide_k, np.full(wide_k.size, -0.006), "|", ms=5, color=SLATE,
            label="wide floor grid")
    ax.plot(data_k, np.full(data_k.size, -0.022), "|", ms=5, color=TEAL,
            label="confined floor grid")
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel(r"total variance $w(k)$")
    ax.legend(fontsize=8, loc="center right")
    callout(ax, "phantom: near extrapolates\nabove far, no data here",
            xy=(-0.80, float(near_steep.total_variance(-0.80))),
            xytext=(-0.55, 0.31))
    label_panel(ax, "A")
    ax = axes[1]
    kz = np.linspace(k_narrow.min() - 0.03, k_narrow.max() + 0.03, 200)
    ax.scatter(k_narrow, 100 * np.sqrt(w_far / t), s=14, color="black",
               zorder=5, label="far quotes")
    ax.plot(kz, 100 * wide.raw.implied_vol(kz, t), color=RUST, ls="--",
            lw=1.8, label="fit under the wide floor")
    ax.plot(kz, 100 * data.raw.implied_vol(kz, t), color=TEAL, lw=1.9,
            label="fit under the confined floor")
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel("implied volatility (%)")
    ax.legend(fontsize=8, loc="upper right")
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.30)
    save(fig, OUT / "fig_calm_phantom.pdf")


# ---------------------------------------------------------------------------
# Appendix verification: the confined-grid listing against production.
# ---------------------------------------------------------------------------


def listing_variance_floor_grid_from(k_quotes, n=41):
    k = np.asarray(k_quotes, float)
    if k.size == 0:
        return variance_floor_grid()
    return np.linspace(float(k.min()), float(k.max()), n)


def verify_listings() -> None:
    d = 0.0
    for probe in (np.linspace(-0.25, 0.20, 21), np.array([0.05]),
                  np.array([])):
        a = listing_variance_floor_grid_from(probe)
        b = variance_floor_grid_from(probe)
        d = max(d, float(np.max(np.abs(a - b))))
    sci_macro("calmrefagree", d)


