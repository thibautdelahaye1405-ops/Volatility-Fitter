def smile_w(k, atm_vol, wing, t):
    vol = atm_vol + wing * (np.sqrt(k * k + 0.01) - 0.1) - 0.10 * k
    return (vol**2) * t


K_PAIR = np.linspace(-0.32, 0.28, 21)


def figure_faces() -> None:
    near = calibrate_slice(K_PAIR, smile_w(K_PAIR, 0.20, 0.30, 0.25), 0.25,
                           n_order=6, reg_lambda=1e-6)
    far = calibrate_slice(K_PAIR, smile_w(K_PAIR, 0.21, 0.30, 0.50), 0.50,
                          n_order=6, reg_lambda=1e-6)
    kk = np.linspace(-0.60, 0.50, 300)
    c_n, c_f = near.slice.call_price(kk), far.slice.call_price(kk)
    sci_macro("calmfacesminspread", float(np.min(c_f - c_n)))

    fig, axes = plt.subplots(2, 2, figsize=(7.6, 6.4))
    ax = axes[0, 0]
    spread = 1e4 * (c_f - c_n)
    ax.fill_between(kk, spread, color=TEAL, alpha=0.18, lw=0)
    ax.plot(kk, spread, color=TEAL, lw=1.9)
    ax.axhline(0.0, color=PALETTE["ink"], lw=0.8, ls=":")
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel(r"calendar spread $C_{T_2}-C_{T_1}$ ($10^{-4}$)")
    ax.set_ylim(bottom=-6)
    label_panel(ax, "A")
    ax = axes[0, 1]
    for res, col, lab in ((near, SLATE, "near"), (far, TEAL, "far")):
        x, f = res.slice.density()
        m = (x >= -0.8) & (x <= 0.7)
        ax.plot(x[m], f[m], color=col, lw=1.8, label=lab)
    ax.set_xlabel(r"log return $x$")
    ax.set_ylabel(r"density $f_X$")
    ax.legend(fontsize=8.5, loc="upper right")
    label_panel(ax, "B")
    ax = axes[1, 0]
    u = expit(near.slice.z)
    sel = (u > 0.005) & (u < 0.995)
    gdiff = 1e4 * (far.slice.a_z[sel] - near.slice.a_z[sel])
    ax.fill_between(u[sel], gdiff, color=TEAL, alpha=0.18, lw=0)
    ax.plot(u[sel], gdiff, color=TEAL, lw=1.9)
    ax.axhline(0.0, color=PALETTE["ink"], lw=0.8, ls=":")
    ax.set_xlabel(r"quantile level $\alpha$")
    ax.set_ylabel(r"$G_{T_2}-G_{T_1}$ ($10^{-4}$)")
    ax.set_ylim(bottom=-6)
    label_panel(ax, "C")
    ax = axes[1, 1]
    ax.plot(kk, near.slice.implied_w(kk), color=SLATE, lw=1.8, label="near $w$")
    ax.plot(kk, far.slice.implied_w(kk), color=TEAL, lw=1.9, label="far $w$")
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel(r"total variance $w(k)$")
    ax.legend(fontsize=8.5, loc="upper right")
    label_panel(ax, "D")
    fig.subplots_adjust(wspace=0.30, hspace=0.34)
    save(fig, OUT / "fig_calm_faces.pdf")


# ---------------------------------------------------------------------------
# Figure 3: the phantom drag under four floors — why price space.
# The exact acute scenario of the shipped confinement lock.
# ---------------------------------------------------------------------------

T_NEAR, T_FAR = 0.02, 0.25
K_NEAR = np.linspace(-0.06, 0.06, 13)
W_NEAR = 0.0008 + 0.6 * K_NEAR**2
K_FAR = np.linspace(-0.30, 0.30, 25)
W_FAR = 0.010 + 0.004 * K_FAR**2


def figure_drag() -> None:
    near = calibrate_slice(K_NEAR, W_NEAR, t=T_NEAR)
    window = common_support(K_NEAR, K_FAR)
    cz, cfloor = calendar_floor_targets(near.slice)
    q_at_cz = np.interp(cz, near.slice.z, near.slice.q_z)
    win_mask = (q_at_cz >= window[0]) & (q_at_cz <= window[1])
    ck, cpfloor, ctaper = confined_calendar_floor(near.slice, window)

    fits = {
        "free": calibrate_slice(K_FAR, W_FAR, t=T_FAR),
        "full": calibrate_slice(K_FAR, W_FAR, t=T_FAR,
                                calendar_z=cz, calendar_floor=cfloor),
        "win": calibrate_slice(K_FAR, W_FAR, t=T_FAR,
                               calendar_z=cz[win_mask],
                               calendar_floor=cfloor[win_mask]),
        "price": calibrate_slice(K_FAR, W_FAR, t=T_FAR, calendar_k=ck,
                                 calendar_price_floor=cpfloor,
                                 calendar_taper=ctaper),
    }
    errs = {n: max_iv_err_bp(r, K_FAR, W_FAR, T_FAR) for n, r in fits.items()}
    macro("calmdragfreebp", "%.1f", errs["free"])
    macro("calmdragfullbp", "%.0f", errs["full"])
    macro("calmdragwinbp", "%.0f", errs["win"])
    macro("calmdragpricebp", "%.1f", errs["price"])

    fig, axes = plt.subplots(1, 2, figsize=WIDE,
                             gridspec_kw={"width_ratios": [1.25, 1.0]})
    ax = axes[0]
    kk = np.linspace(-0.34, 0.34, 400)
    ax.axvspan(window[0], window[1], color=AMBER, alpha=0.16, lw=0,
               label="common support")
    ax.plot(kk, near.slice.implied_w(kk), color=RUST, lw=1.8,
            label="acute near slice (fit + extrapolation)")
    ax.plot(kk, np.interp(kk, K_FAR, W_FAR), color=TEAL, lw=1.8,
            label="far slice quotes (true)")
    ax.plot(K_NEAR, W_NEAR, "o", ms=3.0, color=RUST)
    ax.plot(K_FAR, W_FAR, "o", ms=3.0, color=TEAL)
    ax.set_ylim(0, 0.028)
    ax.set_xlabel(r"log-moneyness $k$")
    ax.set_ylabel(r"total variance $w(k)$")
    ax.legend(fontsize=8, loc="upper center")
    label_panel(ax, "A")
    ax = axes[1]
    names = ["no floor", "full-grid\n$G$ floor", "$z$-windowed\n$G$ floor",
             "confined\nprice floor"]
    vals = [errs["free"], errs["full"], errs["win"], errs["price"]]
    cols = [SLATE, RUST, AMBER, TEAL]
    bars = ax.bar(names, vals, color=cols, width=0.62)
    for b, v in zip(bars, vals):
        ax.annotate(f"{v:.0f}", xy=(b.get_x() + b.get_width() / 2, v),
                    xytext=(0, 3), textcoords="offset points", ha="center",
                    fontsize=8.5)
    ax.set_ylabel("far-fit worst quote error (vol bp)")
    ax.tick_params(axis="x", labelsize=8)
    label_panel(ax, "B")
    fig.subplots_adjust(wspace=0.30)
    save(fig, OUT / "fig_calm_drag.pdf")


